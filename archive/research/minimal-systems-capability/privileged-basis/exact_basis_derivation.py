def read_premise_authority(
    exact_rows: list[dict[str, str]], routes: dict[str, dict[str, str]]
) -> dict[str, dict[str, str]]:
    raw = PREMISE_AUTHORITY_PATH.read_bytes()
    if b"\r" in raw or not raw.endswith(b"\n"):
        raise VerificationError("premise authority must use strict LF termination")
    with PREMISE_AUTHORITY_PATH.open(newline="") as handle:
        header = tuple(csv.DictReader(handle, delimiter="\t").fieldnames or ())
    if header != PREMISE_AUTHORITY_FIELDS:
        raise VerificationError("premise authority header differs from schema")
    rows = read_tsv(PREMISE_AUTHORITY_PATH)
    by_member = {row["member_contract_id"]: row for row in rows}
    source_members = {row["member_contract_id"] for row in exact_rows}
    if len(rows) != 93 or len(by_member) != 93 or set(by_member) != source_members:
        raise VerificationError(
            "premise authority must be an exact, unique 93-member partition"
        )
    for row in rows:
        if row["schema_version"] != "dense-premise-authority-v1":
            raise VerificationError("premise authority schema changed")
        if row["authority_id"] != f"PREMISE::{row['member_contract_id']}":
            raise VerificationError(
                f"premise authority ID mismatch for {row['member_contract_id']}"
            )
        if row["authority_status"] != "RESEARCH_AUTHORITY_PENDING_OWNER_REVIEW":
            raise VerificationError(
                f"premise authority status changed for {row['member_contract_id']}"
            )
        for field in (
            "behavior_route_id",
            "required_normal_route_ids",
            "required_protocol_route_ids",
        ):
            for route_id in split_ids(row[field]):
                if route_id not in routes:
                    raise VerificationError(
                        f"premise authority for {row['member_contract_id']} names unknown route {route_id}"
                    )
        behavior = row["behavior_route_id"]
        if behavior != "NONE" and not behavior.startswith("CALLABLE-"):
            raise VerificationError(
                f"premise authority behavior route is not callable for {row['member_contract_id']}"
            )
    return by_member


