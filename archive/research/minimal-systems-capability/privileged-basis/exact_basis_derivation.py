def verify_crosswalk_and_routes() -> tuple[dict[str, dict[str, str]], dict[str, dict[str, str]]]:
    obligations = read_tsv(OBLIGATION_PATH)
    crosswalk_rows = read_tsv(CROSSWALK_PATH)
    route_rows = read_tsv(ROUTE_PATH)
    crosswalk = {row["capability_id"]: row for row in crosswalk_rows}
    routes = {row["route_id"]: row for row in route_rows}
    if len(crosswalk) != len(crosswalk_rows):
        raise VerificationError("duplicate capability_id in crosswalk")
    if len(routes) != len(route_rows):
        raise VerificationError("duplicate route_id in route registry")

    expected_ids = {row["capability_id"] for row in obligations} | set(EXPECTED_CROSSWALK_ALIASES) | {"NONE"}
    if set(crosswalk) != expected_ids:
        raise VerificationError(
            f"crosswalk IDs differ: missing={sorted(expected_ids-set(crosswalk))} extra={sorted(set(crosswalk)-expected_ids)}"
        )

    allowed_classes = {
        "CORE_RULE",
        "DERIVED_PROOF",
        "ORDINARY_PROTOCOL",
        "ADMITTED_ENTRY",
        "PROOF_FACT",
        "PERFORMANCE_OBLIGATION",
        "ALIAS",
        "NEGATIVE_DISPOSITION",
    }
    for row in crosswalk_rows:
        if row["route_class"] not in allowed_classes:
            raise VerificationError(f"undefined route class {row['route_class']} for {row['capability_id']}")
        for route in split_ids(row["route_ids"]):
            if route not in routes:
                raise VerificationError(f"crosswalk {row['capability_id']} names unknown route {route}")
    for alias, targets in EXPECTED_CROSSWALK_ALIASES.items():
        row = crosswalk[alias]
        if row["mapping_kind"] != "ALIAS" or tuple(split_ids(row["alias_target_ids"])) != targets:
            raise VerificationError(f"alias {alias} does not exactly target {targets}")
    for capability in ("NT-FIXED", "NT-P2"):
        row = crosswalk[capability]
        if row["evidence_status"] != "PENDING_MEASUREMENT" or row["route_class"] != "PERFORMANCE_OBLIGATION":
            raise VerificationError(f"{capability} must remain a pending performance obligation")
    if crosswalk["NONE"]["evidence_status"] != "NEGATIVE_ONLY":
        raise VerificationError("NONE must be a negative-only sentinel")
    return crosswalk, routes


def verify_generated_bytes(expected: bytes) -> list[dict[str, str]]:
    if not OUTPUT_PATH.exists():
        raise VerificationError(f"missing generated output {OUTPUT_PATH}")
    actual = OUTPUT_PATH.read_bytes()
    if actual != expected:
        raise VerificationError("generated exact derivation registry is stale; run build")
    return read_tsv(OUTPUT_PATH)


def verify_output(rows: list[dict[str, str]], routes: dict[str, dict[str, str]]) -> dict[str, int]:
    exact_rows = read_tsv(EXACT_PATH)
    authority_rows = read_tsv(AUTHORITY_PATH)
    _, outcome_authority_by_key = read_outcome_authority(exact_rows, routes)
    groups = member_group_map()
    exact_members = {row["member_contract_id"] for row in exact_rows}
    if set(groups) != exact_members or len(groups) != 93:
        raise VerificationError(
            f"member classification is not exact: classified={len(groups)} source={len(exact_members)}"
        )
    if len(rows) != 303 or len({row["contract_id"] for row in rows}) != 303:
        raise VerificationError("exact registry must contain 303 unique contract rows")
    if Counter(row["source_status"] for row in rows) != Counter(EXPECTED_STATUS_COUNTS):
        raise VerificationError(f"source status counts changed: {Counter(row['source_status'] for row in rows)}")
    if any(row["candidate_execution_authorized"] != "NO" for row in rows):
        raise VerificationError("candidate execution authorization changed")

    source_by_contract = {row["contract_id"]: row for row in exact_rows}
    copied_fields = {
        "member_contract_id": "member_contract_id",
        "outcome_id": "outcome_id",
        "cluster_id": "cluster_id",
        "source_status": "status",
        "policy_variant_id": "policy_variant_id",
        "profile_id": "profile_id",
        "od3_policy_foreign_key": "zst_policy_foreign_key",
        "od4_policy_options": "od4_policy_options",
        "candidate_execution_authorized": "candidate_execution_authorized",
        "legacy_capability_ids": "capability_ids",
        "payload_branch_ids": "payload_branch_ids",
        "stored_borrow_route_ids": "stored_borrow_route_ids",
        "behavior_call_count_order_effects": "behavior_call_count_order_effects",
        "behavior_calls": "behavior_calls",
        "trigger": "trigger",
        "pre_state": "pre_state",
        "post_state": "post_state",
        "commitment_point": "commitment_point",
        "commit_phase": "commit_phase",
        "offered_owners": "offered_owners",
        "result_owners": "result_owners",
        "returned_owners": "returned_owners",
        "retained_owners": "retained_owners",
        "destroyed_owners": "destroyed_owners",
        "owners_before": "owners_before",
        "owners_after": "owners_after",
        "state_equation": "state_equation",
        "allocation_disposition": "allocation_disposition",
        "allocation_before": "allocation_before",
        "allocation_after": "allocation_after",
        "borrows_before": "borrows_before",
        "borrows_after": "borrows_after",
        "facts_before": "facts_before",
        "facts_after": "facts_after",
        "normal_exit_cleanup": "normal_exit_cleanup",
        "pre_abort_invariant": "pre_abort_invariant",
        "resource_ceiling": "resource_ceiling",
        "member_declaration_sha256": "member_declaration_sha256",
    }
    for row in rows:
        source = source_by_contract[row["contract_id"]]
        for output_field, source_field in copied_fields.items():
            if row[output_field] != source[source_field]:
                raise VerificationError(f"{row['contract_id']} changed exact field {output_field}")
        unresolved_authority_rows = [
            outcome_authority_by_key[(row["contract_id"], route_id)]
            for route_id in OUTCOME_AUTHORITY_ROUTE_IDS
            if outcome_authority_by_key[(row["contract_id"], route_id)][
                "disposition"
            ]
            == "UNRESOLVED_WITH_BLOCKER"
        ]
        if unresolved_authority_rows:
            if (
                row["basis_disposition"] != PENDING_EXACT_DIRECTION_STATUS
                or row["structural_evidence_status"]
                != PENDING_EXACT_DIRECTION_STATUS
            ):
                raise VerificationError(
                    f"{row['contract_id']} credits an unresolved exact direction"
                )
            expected_blockers = {
                blocker_id
                for authority in unresolved_authority_rows
                for blocker_id in split_ids(authority["blocker_ids"])
            } | {"EXACT-OUTCOME-DIRECTION-UNRESOLVED"}
            if not expected_blockers <= set(split_ids(row["blocker_ids"])):
                raise VerificationError(
                    f"{row['contract_id']} loses an unresolved exact-direction blocker"
                )
        else:
            expected_disposition, expected_blockers = basis_disposition(source)
            if (
                row["basis_disposition"] != expected_disposition
                or row["blocker_ids"] != expected_blockers
                or row["structural_evidence_status"]
                != structural_status(source["status"])
            ):
                raise VerificationError(
                    f"{row['contract_id']} changes its source-derived evidence status"
                )
        for route in (
            split_ids(row["member_allowed_route_ids"])
            + split_ids(row["outcome_required_or_available_route_ids"])
            + split_ids(row["payload_overlay_route_ids"])
        ):
            if route not in routes:
                raise VerificationError(f"{row['contract_id']} names unknown route {route}")
        if row["exit_route_id"] not in routes:
            raise VerificationError(f"{row['contract_id']} names unknown exit route")
        if row["member_behavior_route_id"] != "NONE" and row["member_behavior_route_id"] not in routes:
            raise VerificationError(f"{row['contract_id']} names unknown behavior route")
        material = "\t".join(row[field] for field in OUTPUT_FIELDS if field != "derivation_row_sha256")
        if row["derivation_row_sha256"] != hashlib.sha256(material.encode()).hexdigest():
            raise VerificationError(f"{row['contract_id']} row hash mismatch")

    # Exact alias and hidden-premise guards.
    for row in rows:
        member = row["member_contract_id"]
        algorithm = set(split_ids(row["member_allowed_route_ids"]))
        selected = set(split_ids(row["outcome_required_or_available_route_ids"]))
        premise = row["member_behavior_premise_kind"]
        if member in MEMBER_GROUPS["COPY"]:
            if "CORE-COPY" not in algorithm or "CALLABLE-CLONE" in algorithm or premise != "CORE_COPY":
                raise VerificationError(f"{member} is not routed exclusively through CoreCopy")
        if member in MEMBER_GROUPS["CLONE_BUILD"] | MEMBER_GROUPS["CLONE_MUTATE"]:
            if "CALLABLE-CLONE" not in algorithm or premise != "CLONE":
                raise VerificationError(f"{member} lost its explicit Clone premise")
        if member in {"DENSE-FILL-WITH", "DENSE-RESIZE-WITH"}:
            if "CALLABLE-PRODUCER" not in algorithm or "CALLABLE-CLONE" in algorithm or premise != "PRODUCER":
                raise VerificationError(f"{member} silently aliases producer behavior to Clone")
        if member == "DENSE-INTO-FLATTENED":
            if algorithm != {"CORE-RESHAPE-PARTITION", "FAIL-CHECKED-ARITH", "PROTOCOL-OPAQUE-OWNER"}:
                raise VerificationError("DENSE-INTO-FLATTENED must use only proof reshape plus its check/owner routes")
        if member == "DENSE-INTO-OWNER":
            if algorithm != {
                "CORE-RESHAPE-PARTITION",
                "CORE-REPACKAGE-FULL-STORAGE",
                "PROTOCOL-OPAQUE-OWNER",
            }:
                raise VerificationError(
                    "DENSE-INTO-OWNER must expose reshape plus full-storage repackaging"
                )
        if member == "DENSE-DEFAULT" and premise != "NONE":
            raise VerificationError("empty-owner Default must not invoke Default<T>")
        if member == "DENSE-TAKE-WITH-DEFAULT" and premise != "DEFAULT":
            raise VerificationError("take-with-default lost its explicit Default premise")
        if row["behavior_calls"] == "ZERO" and any(route.startswith("CALLABLE-") for route in selected):
            raise VerificationError(f"{row['contract_id']} activates a callable on a zero-call outcome")
        if row["outer_allocator_route_applicability"].startswith("FORBIDDEN") and "FAIL-ALLOC-EXACT" in selected:
            raise VerificationError(f"{row['contract_id']} activates the allocator on a forbidden outcome")
        if row["outer_allocator_route_applicability"] == "BEHAVIOR_OWNED_ONLY_OUTER_FORBIDDEN" and "FAIL-ALLOC-EXACT" in selected:
            raise VerificationError(f"{row['contract_id']} attributes behavior-owned allocation to the outer route")
        if member == "DENSE-INTO-FLATTENED" and outcome_path_kind(source_by_contract[row["contract_id"]]) != "NORMAL":
            if "CORE-RESHAPE-PARTITION" in selected:
                raise VerificationError("failed reshape precondition activates reshape")
        if member == "DENSE-INTO-OWNER" and outcome_path_kind(source_by_contract[row["contract_id"]]) == "NORMAL":
            if "FAIL-ALLOC-EXACT" in selected or any(route.startswith("CALLABLE-") for route in selected):
                raise VerificationError("full-storage adoption gains allocation or callable premise")

    # Stored-borrow and authoritative-join guards.
    active_stored = [row for row in rows if row["stored_borrow_route_ids"] != "NONE"]
    if len(active_stored) != 30:
        raise VerificationError(f"expected 30 active stored-borrow rows, got {len(active_stored)}")
    for row in active_stored:
        if "PROTOCOL-STORED-BORROW" not in split_ids(row["payload_overlay_route_ids"]):
            raise VerificationError(f"{row['contract_id']} loses active stored-borrow route")
    overlay_source = [row for row in authority_rows if row["binding_kind"] == "DEFERRED_OVERLAY"]
    if Counter(row["capability_id"] for row in overlay_source) != Counter(
        {"BR-STORED": 91, "BR-PROV": 65, "BR-RESULT": 23}
    ):
        raise VerificationError("source deferred-overlay population changed")

    # Negative and policy rows never become a positive PASS.
    for row in rows:
        if row["source_status"] != "REQUIRED_IN_LOCK" and row["basis_disposition"].startswith("PAPER_ROUTE_COVERED"):
            raise VerificationError(f"{row['contract_id']} erases source blocker or exclusion")
        if row["source_status"] == "EXCLUDED_BLOCKS_NAMED_CLAIM" and row["structural_evidence_status"] != "NO_POSITIVE_COST_CREDIT":
            raise VerificationError(f"{row['contract_id']} credits an excluded runtime route")
        if row["measurement_status"] != "NOT_AUTHORIZED_NOT_RUN":
            raise VerificationError(f"{row['contract_id']} invents a measurement")

    alias_counts = Counter()
    for row in rows:
        alias_counts.update(split_ids(row["legacy_alias_mismatch_ids"]))
    expected_alias_counts = Counter(
        {
            "OW-RELOCATE-AS-CORE-COPY": 4,
            "OW-CLONE-AS-CORE-COPY": 3,
            "OW-CLONE-AS-PRODUCER": 12,
            "OWNER-TRAFFIC-AS-RESHAPE": 2,
            "OW-CLONE-AS-OWNER-REPACK": 1,
            "OWNER-TRAFFIC-AS-OWNER-REPACK": 1,
            "ALLOC-CAPABILITY-ON-ZERO-ALLOC-REPACK": 1,
            "OW-INIT-AS-METADATA-FORMATION": 3,
        }
    )
    if alias_counts != expected_alias_counts:
        raise VerificationError(f"legacy alias mismatch population changed: {alias_counts}")

    return {
        "rows": len(rows),
        "members": len({row["member_contract_id"] for row in rows}),
        "required": sum(row["source_status"] == "REQUIRED_IN_LOCK" for row in rows),
        "excluded": sum(row["source_status"] == "EXCLUDED_BLOCKS_NAMED_CLAIM" for row in rows),
        "od4_conditional": sum("OD-4" in row["source_status"] for row in rows),
        "unknown_length_blocked": sum("UNKNOWN_LENGTH" in row["source_status"] for row in rows),
        "active_stored_borrow": len(active_stored),
        "legacy_alias_flags": sum(alias_counts.values()),
    }


def verify_table_shape_and_hashes(
    path: Path,
    fields: tuple[str, ...],
    row_hash_field: str,
) -> list[dict[str, str]]:
    raw = path.read_bytes()
    try:
        raw.decode("utf-8")
    except UnicodeDecodeError as error:
        raise VerificationError(f"{path.name} is not strict UTF-8: {error}") from error
    if b"\r" in raw or not raw.endswith(b"\n"):
        raise VerificationError(f"{path.name} must use strict LF termination")
    rows = read_tsv(path)
    with path.open(newline="") as handle:
        header = tuple(csv.DictReader(handle, delimiter="\t").fieldnames or ())
    if header != fields:
        raise VerificationError(f"{path.name} header differs from schema")
    digest_fields = [field for field in fields if field != row_hash_field]
    for row in rows:
        if row[row_hash_field] != row_sha256(row, digest_fields):
            raise VerificationError(f"{path.name} row hash mismatch: {row.get(row_hash_field, 'NONE')}")
    return rows


def member_authority_mismatch_count(
    members: list[dict[str, str]],
    premise_authority: dict[str, dict[str, str]],
) -> int:
    mismatches = 0
    for member in members:
        authority = premise_authority.get(member["member_contract_id"])
        if authority is None:
            mismatches += 1
            continue
        if member["premise_class"] != authority["premise_class"]:
            mismatches += 1
        if member["source_preservation_rule"] != authority["source_preservation_rule"]:
            mismatches += 1
        allowed = set(split_ids(member["allowed_route_ids"]))
        independently_required = set(
            split_ids(authority["required_normal_route_ids"])
            + split_ids(authority["required_protocol_route_ids"])
        )
        if authority["behavior_route_id"] != "NONE":
            independently_required.add(authority["behavior_route_id"])
            if member["allowed_call_kinds"] != authority["behavior_route_id"]:
                mismatches += 1
        if not independently_required <= allowed:
            mismatches += 1
    return mismatches


