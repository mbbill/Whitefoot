def mutation_obligation_failures(
    steps: list[dict[str, str]], obligations: list[dict[str, str]]
) -> tuple[int, int]:
    steps_by_key: dict[tuple[str, str], dict[str, str]] = {}
    for step in steps:
        key = (step["context_id"], step["route_id"])
        if key in steps_by_key:
            raise VerificationError(
                f"hostile action relation repeats context/route key {key}"
            )
        steps_by_key[key] = step
    missing = violations = 0
    for obligation in obligations:
        step = steps_by_key.get(
            (obligation["context_id"], obligation["route_id"])
        )
        activated = bool(step) and route_applicability_is_activated(
            step["route_applicability"]
        )
        unresolved = bool(step) and step["route_applicability"].startswith(
            "UNRESOLVED"
        )
        conditional = bool(step) and step["route_applicability"].startswith(
            "CONDITIONAL_"
        )
        choice = bool(step) and step["route_applicability"].startswith("CHOICE_")
        if obligation["obligation_kind"] == "REQUIRE_ACTIVATED" and not activated:
            missing += 1
        elif obligation["obligation_kind"] == "FORBID_ACTIVATED" and activated:
            violations += 1
        elif obligation["obligation_kind"] == "REQUIRE_UNRESOLVED" and not unresolved:
            missing += 1
        elif (
            obligation["obligation_kind"] == "CONDITIONAL_AVAILABLE_NO_CREDIT"
            and not conditional
        ):
            missing += 1
        elif (
            obligation["obligation_kind"] == "REQUIRE_ONE_OF_NO_CREDIT"
            and not choice
        ):
            missing += 1
    return missing, violations


def mutation_obligation_relation_errors(
    expected: list[dict[str, str]], actual: list[dict[str, str]]
) -> int:
    expected_rows = Counter(
        tuple(row[field] for field in ROUTE_OBLIGATION_FIELDS) for row in expected
    )
    actual_rows = Counter(
        tuple(row[field] for field in ROUTE_OBLIGATION_FIELDS) for row in actual
    )
    return sum((expected_rows - actual_rows).values()) + sum(
        (actual_rows - expected_rows).values()
    )


def mutation_context_relation_errors(
    exact_rows: list[dict[str, str]], contexts: list[dict[str, str]]
) -> int:
    expected = {
        (exact["contract_id"], payload, size_class, stored, od3, od4)
        for exact in exact_rows
        for payload, size_class, stored, od3, od4 in context_dimensions(exact)
    }
    actual_rows = [
        (
            row["contract_id"],
            row["payload_branch_id"],
            row["payload_size_class"],
            row["stored_borrow_route_id"],
            row["od3_policy_variant_id"],
            row["od4_policy_option"],
        )
        for row in contexts
    ]
    actual = set(actual_rows)
    return len(expected ^ actual) + (len(actual_rows) - len(actual))


def mutation_discharge_mismatches(
    contexts: list[dict[str, str]],
    steps: list[dict[str, str]],
    discharges: list[dict[str, str]],
    routes: dict[str, dict[str, str]],
) -> int:
    context_by_id = {row["context_id"]: row for row in contexts}
    active: dict[str, set[str]] = defaultdict(set)
    unresolved: dict[str, set[str]] = defaultdict(set)
    conditional: dict[str, set[str]] = defaultdict(set)
    choice: dict[str, set[str]] = defaultdict(set)
    forbidden: dict[str, set[str]] = defaultdict(set)
    for step in steps:
        if route_applicability_is_activated(step["route_applicability"]):
            active[step["context_id"]].add(step["route_id"])
        elif step["route_applicability"].startswith("UNRESOLVED"):
            unresolved[step["context_id"]].add(step["route_id"])
        elif step["route_applicability"].startswith("CONDITIONAL_"):
            conditional[step["context_id"]].add(step["route_id"])
        elif step["route_applicability"].startswith("CHOICE_"):
            choice[step["context_id"]].add(step["route_id"])
        elif step["route_applicability"].startswith("FORBIDDEN"):
            forbidden[step["context_id"]].add(step["route_id"])
    rejected = {
        "SOURCE_NEGATIVE",
        "OD3_DEFER_ZST_REJECTION",
        "OD3_INCLUDE_ZST_OUTCOME_INAPPLICABLE",
    }
    positive_routes = {
        route_id
        for route_id, route in routes.items()
        if route["route_class"] != "NEGATIVE_DISPOSITION"
    }
    mismatches = 0
    for discharge in discharges:
        if discharge["scope_kind"] != "BRANCH_CONTEXT":
            continue
        context_id = discharge["context_id"]
        available = set(split_ids(discharge["crosswalk_route_ids"]))
        expected_active = available & active[context_id]
        expected_unresolved = available & unresolved[context_id]
        expected_conditional = available & conditional[context_id]
        expected_choice = available & choice[context_id]
        expected_forbidden = available & forbidden[context_id]
        if discharge["source_inconsistency_ids"] != "NONE":
            expected_active = set()
            expected_unresolved = set()
            expected_conditional = set()
            expected_choice = set()
            expected_forbidden = set()
        actual_active = set(split_ids(discharge["outcome_action_route_ids"]))
        actual_unresolved = set(split_ids(discharge["unresolved_outcome_route_ids"]))
        actual_conditional = set(split_ids(discharge["conditional_outcome_route_ids"]))
        actual_choice = set(split_ids(discharge["choice_outcome_route_ids"]))
        actual_forbidden = set(split_ids(discharge["forbidden_outcome_route_ids"]))
        if (
            actual_active != expected_active
            or actual_unresolved != expected_unresolved
            or actual_conditional != expected_conditional
            or actual_choice != expected_choice
            or actual_forbidden != expected_forbidden
        ):
            mismatches += 1
        if (
            context_by_id[context_id]["context_kind"] in rejected
            and actual_active & positive_routes
        ):
            mismatches += 1
    return mismatches


