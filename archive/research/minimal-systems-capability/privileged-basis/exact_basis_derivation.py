    conditional_obligation = next(iter(conditional_canary_rows.values()))[0]
    conditional_steps = [dict(step) for step in steps]
    conditional_step = next(
        step
        for step in conditional_steps
        if step["context_id"] == conditional_obligation["context_id"]
        and step["route_id"] == conditional_obligation["route_id"]
    )
    conditional_step["route_applicability"] = (
        "REQUIRED_MUTATION_CONDITIONAL_ROUTE_AS_UNCONDITIONAL_CREDIT"
    )
    if mutation_obligation_failures(conditional_steps, obligations)[0] == 0:
        raise VerificationError(
            "hostile conditional-to-unconditional action-credit mutation was not detected"
        )

    conditional_obligations = [dict(row) for row in obligations]
    conditional_provenance = next(
        row
        for row in conditional_obligations
        if row["obligation_id"] == conditional_obligation["obligation_id"]
    )
    conditional_provenance["predicate_id"] = next(
        predicate_id
        for predicate_id in FROZEN_OUTCOME_PREDICATES
        if predicate_id != conditional_provenance["predicate_id"]
    )
    if mutation_obligation_relation_errors(
        expected_obligations, conditional_obligations
    ) == 0:
        raise VerificationError(
            "hostile conditional predicate-FK mutation was not detected"
        )

    missing_outcome_authority_row = outcome_authority_rows[1:]
    if {
        (row["contract_id"], row["route_id"])
        for row in missing_outcome_authority_row
    } == expected_outcome_authority_keys(exact_rows):
        raise VerificationError(
            "hostile missing outcome-authority complement mutation was not detected"
        )

    mutated_obligations = [dict(row) for row in obligations]
    provenance_obligation = next(
        row
        for row in mutated_obligations
        if row["authority_kind"] == "EXACT_OUTCOME_ROUTE_AUTHORITY"
    )
    provenance_obligation["outcome_authority_entry_sha256"] = "0" * 64
