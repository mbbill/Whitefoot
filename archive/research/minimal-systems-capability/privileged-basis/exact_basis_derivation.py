def frozen_focus_obligations(
    member_contract_id: str, outcome_suffix_id: str
) -> tuple[str, ...] | None:
    if member_contract_id in {"DENSE-CLEAR", "DENSE-DROP", "DENSE-TRUNCATE"}:
        tail = (
            "FOCUS-PRE-ABORT-SAFE"
            if outcome_suffix_id == "BEHAVIOR_ABORT"
            else "FOCUS-CONSUME-OR-RESEAL"
        )
        return (
            "FOCUS-OPEN-SEALED-OWNER",
            "FOCUS-DISPOSE-EXACT-LIVE-SET",
            tail,
        )
    if member_contract_id in {"DENSE-INSERT", "DENSE-INSERT-UNIQ"}:
        if outcome_suffix_id == "SUCCESS_GROW":
            return (
                "FOCUS-OPEN-SEALED-OWNER",
                "FOCUS-RELOCATE-LIVE-PREFIX",
                "FOCUS-INSERT-OFFERED-OWNER",
                "FOCUS-RESEAL-NEW-CARRIER",
                "FOCUS-RELEASE-OLD-CARRIER",
            )
        return (
            "FOCUS-OPEN-SEALED-OWNER",
            "FOCUS-SHIFT-SUFFIX",
            "FOCUS-INSERT-OFFERED-OWNER",
            "FOCUS-RESEAL-DENSE-PREFIX",
        )
    if member_contract_id in {"DENSE-PUSH", "DENSE-PUSH-UNIQ"}:
        if outcome_suffix_id == "SUCCESS_GROW":
            return (
                "FOCUS-OPEN-SEALED-OWNER",
                "FOCUS-RELOCATE-LIVE-PREFIX",
                "FOCUS-PUT-OFFERED-OWNER",
                "FOCUS-RESEAL-NEW-CARRIER",
                "FOCUS-RELEASE-OLD-CARRIER",
            )
        return (
            "FOCUS-OPEN-SEALED-OWNER",
            "FOCUS-PUT-OFFERED-OWNER",
            "FOCUS-COMMIT-LENGTH",
            "FOCUS-RESEAL-DENSE-PREFIX",
        )
    return FROZEN_FOCUS_FIXED_OBLIGATIONS.get(member_contract_id)


def frozen_exact_route_evidence_tuple(
    contract_id: str,
    member_contract_id: str,
    outcome_suffix_id: str,
    route_id: str,
) -> tuple[str, str, str, str] | None:
    if route_id == "FAIL-CHECKED-ARITH":
        obligations = FROZEN_CHECKED_ARITH_OBLIGATIONS.get(member_contract_id)
        if obligations is None:
            return None
        activation = (
            "EXACT_OUTCOME_SELECTS_FIRST_FAILING_NAMED_OBLIGATION"
            if outcome_suffix_id in FROZEN_CHECKED_ARITH_FIRST_FAILURE_OUTCOMES
            else "EXACT_OUTCOME_REQUIRES_ALL_NAMED_OBLIGATIONS_IN_DECLARED_ORDER"
        )
        return (
            "EXACT_CHECKED_ARITH_OBLIGATION",
            ",".join(obligations),
            activation,
            "DENSE-EXACT-MEMBER-OUTCOME-REGISTRY.tsv::"
            f"{contract_id}::trigger+capability_ids+commitment_point",
        )
    if route_id == "PROTOCOL-EXACT-FOCUS":
        obligations = frozen_focus_obligations(
            member_contract_id, outcome_suffix_id
        )
        if obligations is None:
            return None
        return (
            "EXACT_FOCUS_TRACE_SELECTION",
            ",".join(obligations),
            "EXACT_OUTCOME_REQUIRES_ALL_NAMED_OBLIGATIONS_IN_DECLARED_ORDER",
            "DENSE-EXACT-MEMBER-OUTCOME-REGISTRY.tsv::"
            f"{contract_id}::pre_state+commitment_point+post_state+pre_abort_invariant",
        )
    return None


def exact_route_evidence_matches_frozen(
    row: dict[str, str], exact: dict[str, str]
) -> bool:
    expected = frozen_exact_route_evidence_tuple(
        row["contract_id"],
        row["member_contract_id"],
        outcome_suffix(exact),
        row["route_id"],
    )
    actual = (
        row["evidence_kind"],
        row["exact_obligation_ids"],
        row["activation_rule"],
        row["source_anchor"],
    )
    return expected is not None and actual == expected


def read_exact_route_evidence_authority(
    exact_rows: list[dict[str, str]],
    outcome_authority_by_key: dict[tuple[str, str], dict[str, str]],
    predicate_authority_by_id: dict[str, dict[str, str]],
) -> dict[tuple[str, str], dict[str, str]]:
    verify_frozen_static_authority(EXACT_ROUTE_EVIDENCE_PATH)
    raw = EXACT_ROUTE_EVIDENCE_PATH.read_bytes()
    if b"\r" in raw or not raw.endswith(b"\n"):
        raise VerificationError("exact route evidence authority must use strict LF termination")
    with EXACT_ROUTE_EVIDENCE_PATH.open(newline="") as handle:
        header = tuple(csv.DictReader(handle, delimiter="\t").fieldnames or ())
    if header != EXACT_ROUTE_EVIDENCE_FIELDS:
        raise VerificationError("exact route evidence authority header differs from schema")
    rows = read_tsv(EXACT_ROUTE_EVIDENCE_PATH)
    exact_by_contract = {row["contract_id"]: row for row in exact_rows}
    by_key: dict[tuple[str, str], dict[str, str]] = {}
    seen_ids: set[str] = set()
    previous_id = ""
    for row in rows:
        evidence_id = row["route_evidence_id"]
        if evidence_id in seen_ids:
            raise VerificationError(f"duplicate exact route evidence ID {evidence_id}")
        if previous_id and evidence_id <= previous_id:
            raise VerificationError("exact route evidence rows must sort by route_evidence_id")
        previous_id = evidence_id
        seen_ids.add(evidence_id)
        if row["schema_version"] != EXACT_ROUTE_EVIDENCE_SCHEMA_VERSION:
            raise VerificationError("exact route evidence schema changed")
        exact = exact_by_contract.get(row["contract_id"])
        if exact is None:
            raise VerificationError(
                f"exact route evidence names unknown contract {row['contract_id']}"
            )
        if row["member_contract_id"] != exact["member_contract_id"] or row[
            "outcome_id"
        ] != exact["outcome_id"]:
            raise VerificationError(
                f"exact route evidence source mismatch for {row['contract_id']}"
            )
        route_id = row["route_id"]
        expected_kind = EXACT_ROUTE_EVIDENCE_KINDS.get(route_id)
        if expected_kind is None or row["evidence_kind"] != expected_kind:
            raise VerificationError(
                f"exact route evidence has an invalid route/kind pair for {evidence_id}"
            )
        key = (row["contract_id"], route_id)
        if key in by_key:
            raise VerificationError(f"duplicate exact route evidence key {key}")
        authority = outcome_authority_by_key[key]
        evidence_admitting_disposition = authority["disposition"] in {
            "REQUIRED",
            "REQUIRE_ONE_OF",
        } or (
            authority["disposition"] == "CONDITIONAL_WITH_FROZEN_PREDICATE"
            and predicate_authority_by_id[authority["predicate_id"]]["guard_kind"]
            != "RUNTIME_BRANCH_GUARD"
        )
        if not evidence_admitting_disposition:
            raise VerificationError(
                f"exact route evidence exists without a disposition that can derive REQUIRED at {key}"
            )
        expected_id = f"ROUTE-EVIDENCE::{row['contract_id']}::{route_id}"
        if evidence_id != expected_id or authority["authority_basis"] != evidence_id:
            raise VerificationError(
                f"exact route evidence ID or outcome-authority reference mismatch at {key}"
            )
        obligation_ids = tuple(row["exact_obligation_ids"].split(","))
        if (
            not obligation_ids
            or any(obligation_id in {"", "NONE"} for obligation_id in obligation_ids)
            or len(obligation_ids) != len(set(obligation_ids))
        ):
            raise VerificationError(
                f"exact route evidence obligations are empty, duplicated, or malformed for {evidence_id}"
            )
        if row["activation_rule"] not in EXACT_ROUTE_EVIDENCE_ACTIVATION_RULES:
            raise VerificationError(
                f"exact route evidence activation rule changed for {evidence_id}"
            )
        if row["source_anchor"] in {"", "NONE"}:
            raise VerificationError(f"exact route evidence is unanchored for {evidence_id}")
        if not exact_route_evidence_matches_frozen(row, exact):
            raise VerificationError(
                "exact route evidence differs from the independent closed "
                f"obligation vocabulary for {evidence_id}"
            )
        if row["authority_status"] != OUTCOME_AUTHORITY_STATUS:
            raise VerificationError(f"exact route evidence status changed for {evidence_id}")
        expected_hash = row_sha256(
            row,
            [
                field
                for field in EXACT_ROUTE_EVIDENCE_FIELDS
                if field != "route_evidence_sha256"
            ],
        )
        if row["route_evidence_sha256"] != expected_hash:
            raise VerificationError(f"exact route evidence hash mismatch for {evidence_id}")
        by_key[key] = row

    required_keys = {
        key
        for key, authority in outcome_authority_by_key.items()
        if key[1] in EXACT_ROUTE_EVIDENCE_KINDS
        and (
            authority["disposition"] in {"REQUIRED", "REQUIRE_ONE_OF"}
            or (
                authority["disposition"]
                == "CONDITIONAL_WITH_FROZEN_PREDICATE"
                and predicate_authority_by_id[authority["predicate_id"]][
                    "guard_kind"
                ]
                not in UNOBSERVED_OUTCOME_PREDICATE_GUARD_KINDS
            )
        )
    }
    if set(by_key) != required_keys:
        missing = sorted(required_keys - set(by_key))[:5]
        extra = sorted(set(by_key) - required_keys)[:5]
        raise VerificationError(
            "exact focus/check cells that can derive REQUIRED must have one exact evidence row; "
            f"missing={len(required_keys - set(by_key))} extra={len(set(by_key) - required_keys)} "
            f"first_missing={missing} first_extra={extra}"
        )
    return by_key


def expected_route_multiplicity_minimums(
    exact_rows: list[dict[str, str]],
) -> dict[tuple[str, str], int]:
    result: dict[tuple[str, str], int] = {}
    matched_selectors: set[tuple[str, str, str]] = set()
    for exact in exact_rows:
        selector = (
            exact["member_contract_id"],
            outcome_suffix(exact),
            "CORE-EMPTY-CARRIER",
        )
        minimum = INDEPENDENT_ROUTE_MULTIPLICITY_MINIMUMS.get(selector)
        if minimum is None:
            continue
        matched_selectors.add(selector)
        result[(exact["contract_id"], selector[2])] = minimum
    if matched_selectors != set(INDEPENDENT_ROUTE_MULTIPLICITY_MINIMUMS):
        raise VerificationError(
            "an independent route-multiplicity selector matches no exact contract"
        )
    if len(result) != 5:
        raise VerificationError(
            f"exact route-multiplicity authority must cover 5 cells, got {len(result)}"
        )
    return result


def validate_route_multiplicity_authority_rows(
    rows: list[dict[str, str]],
    exact_rows: list[dict[str, str]],
    outcome_authority_by_key: dict[tuple[str, str], dict[str, str]],
) -> dict[tuple[str, str], dict[str, str]]:
    exact_by_contract = {row["contract_id"]: row for row in exact_rows}
    expected = expected_route_multiplicity_minimums(exact_rows)
    by_key: dict[tuple[str, str], dict[str, str]] = {}
    previous_id = ""
    for row in rows:
        authority_id = row["multiplicity_authority_id"]
        if previous_id and authority_id <= previous_id:
            raise VerificationError(
                "route-multiplicity authority rows must sort by authority ID"
            )
        previous_id = authority_id
        if row["schema_version"] != ROUTE_MULTIPLICITY_AUTHORITY_SCHEMA_VERSION:
            raise VerificationError("route-multiplicity authority schema changed")
        exact = exact_by_contract.get(row["contract_id"])
        if exact is None:
            raise VerificationError(
                f"route-multiplicity authority names unknown contract {row['contract_id']}"
            )
        if row["member_contract_id"] != exact["member_contract_id"] or row[
            "outcome_id"
        ] != exact["outcome_id"]:
            raise VerificationError(
                f"route-multiplicity source mismatch for {authority_id}"
            )
        key = (row["contract_id"], row["route_id"])
        if key in by_key:
            raise VerificationError(f"duplicate route-multiplicity authority key {key}")
        expected_minimum = expected.get(key)
        if expected_minimum is None:
            raise VerificationError(
                f"route-multiplicity authority invents an unfrozen cell {key}"
            )
        if row["minimum_invocation_count"] != str(expected_minimum):
            raise VerificationError(
                f"route-multiplicity lower bound differs for {key}: "
                f"expected={expected_minimum} actual={row['minimum_invocation_count']}"
            )
        if outcome_authority_by_key[key]["disposition"] != "REQUIRED":
            raise VerificationError(
                f"route-multiplicity lower bound attaches to a non-required route {key}"
            )
        if row["evidence_kind"] != ROUTE_MULTIPLICITY_EVIDENCE_KIND:
            raise VerificationError(
                f"route-multiplicity evidence kind changed for {authority_id}"
            )
        expected_anchor = (
            "DENSE-EXACT-MEMBER-OUTCOME-REGISTRY.tsv::"
            f"{row['contract_id']}::result_owners+allocation_disposition+resource_ceiling"
        )
        if row["source_anchor"] != expected_anchor:
            raise VerificationError(
                f"route-multiplicity source anchor changed for {authority_id}"
            )
        if row["authority_status"] != OUTCOME_AUTHORITY_STATUS:
            raise VerificationError(
                f"route-multiplicity authority status changed for {authority_id}"
            )
        expected_id = f"ROUTE-MULTIPLICITY::{row['contract_id']}::{row['route_id']}"
        if authority_id != expected_id:
            raise VerificationError(
                f"route-multiplicity authority ID mismatch for {authority_id}"
            )
        expected_hash = row_sha256(
            row,
            [
                field
                for field in ROUTE_MULTIPLICITY_AUTHORITY_FIELDS
                if field != "multiplicity_authority_sha256"
            ],
        )
        if row["multiplicity_authority_sha256"] != expected_hash:
            raise VerificationError(
                f"route-multiplicity authority hash mismatch for {authority_id}"
            )
        by_key[key] = row
    if set(by_key) != set(expected):
        missing = sorted(set(expected) - set(by_key))[:3]
        extra = sorted(set(by_key) - set(expected))[:3]
        raise VerificationError(
            "route-multiplicity authority must exactly cover the five frozen cells; "
            f"missing={missing} extra={extra}"
        )
    return by_key


def read_route_multiplicity_authority(
    exact_rows: list[dict[str, str]],
    outcome_authority_by_key: dict[tuple[str, str], dict[str, str]],
) -> tuple[list[dict[str, str]], dict[tuple[str, str], dict[str, str]]]:
    verify_frozen_static_authority(ROUTE_MULTIPLICITY_AUTHORITY_PATH)
    raw = ROUTE_MULTIPLICITY_AUTHORITY_PATH.read_bytes()
    if b"\r" in raw or not raw.endswith(b"\n"):
        raise VerificationError(
            "route-multiplicity authority must use strict LF termination"
        )
    with ROUTE_MULTIPLICITY_AUTHORITY_PATH.open(newline="") as handle:
        header = tuple(csv.DictReader(handle, delimiter="\t").fieldnames or ())
    if header != ROUTE_MULTIPLICITY_AUTHORITY_FIELDS:
        raise VerificationError(
            "route-multiplicity authority header differs from schema"
        )
    rows = read_tsv(ROUTE_MULTIPLICITY_AUTHORITY_PATH)
    return rows, validate_route_multiplicity_authority_rows(
        rows, exact_rows, outcome_authority_by_key
    )


CHOICE_RESOLUTION_SELECTOR_FIELDS = (
    "payload_size_class",
    "stored_borrow_route_id",
    "od3_policy_variant_id",
    "od4_policy_option",
)


def choice_resolution_matches(
    context: dict[str, str], resolution: dict[str, str]
) -> bool:
    if context["contract_id"] != resolution["contract_id"] or context[
        "payload_branch_id"
    ] != resolution["payload_branch_id"]:
        return False
    return all(
        resolution[field] == "ALL" or resolution[field] == context[field]
        for field in CHOICE_RESOLUTION_SELECTOR_FIELDS
    )


def choice_resolution_selected_bundle_is_exact(
    resolution: dict[str, str],
    choice_bundles: dict[tuple[str, str], dict[str, set[str]]],
) -> bool:
    alternatives = choice_bundles.get(
        (resolution["contract_id"], resolution["choice_set_id"])
    )
    if alternatives is None:
        return False
    selected_bundle = alternatives.get(resolution["selected_alternative_id"])
    return selected_bundle is not None and resolution[
        "selected_route_ids"
    ] == join_ids(selected_bundle)


def validate_choice_resolution_coverage(
    contexts: list[dict[str, str]],
    choice_bundles: dict[tuple[str, str], dict[str, set[str]]],
    rows: list[dict[str, str]],
) -> tuple[
    dict[tuple[str, str], dict[str, str]],
    set[str],
]:
    rejected_kinds = {
        "SOURCE_NEGATIVE",
        "OD3_DEFER_ZST_REJECTION",
        "OD3_INCLUDE_ZST_OUTCOME_INAPPLICABLE",
    }
    by_context_choice: dict[tuple[str, str], dict[str, str]] = {}
    used_ids: set[str] = set()
    for context in contexts:
        contract_choice_sets = sorted(
            choice_set_id
            for contract_id, choice_set_id in choice_bundles
            if contract_id == context["contract_id"]
        )
        for choice_set_id in contract_choice_sets:
            matches_for_context = [
                row
                for row in rows
                if row["choice_set_id"] == choice_set_id
                and choice_resolution_matches(context, row)
            ]
            if context["context_kind"] in rejected_kinds:
                if matches_for_context:
                    raise VerificationError(
                        f"rejected context {context['context_id']} matches a choice resolution"
                    )
                continue
            if len(matches_for_context) != 1:
                raise VerificationError(
                    f"admitted context {context['context_id']} choice {choice_set_id} "
                    f"matches {len(matches_for_context)} resolutions"
                )
            resolution = matches_for_context[0]
            key = (context["context_id"], choice_set_id)
            by_context_choice[key] = resolution
            used_ids.add(resolution["choice_resolution_id"])
    return by_context_choice, used_ids


def read_choice_resolution_authority(
    exact_rows: list[dict[str, str]],
    contexts: list[dict[str, str]],
    outcome_authority_rows: list[dict[str, str]],
) -> tuple[
    list[dict[str, str]],
    dict[tuple[str, str], dict[str, str]],
]:
    verify_frozen_static_authority(CHOICE_RESOLUTION_PATH)
    raw = CHOICE_RESOLUTION_PATH.read_bytes()
    if b"\r" in raw or not raw.endswith(b"\n"):
        raise VerificationError("choice resolution authority must use strict LF termination")
    with CHOICE_RESOLUTION_PATH.open(newline="") as handle:
        header = tuple(csv.DictReader(handle, delimiter="\t").fieldnames or ())
    if header != CHOICE_RESOLUTION_FIELDS:
        raise VerificationError("choice resolution authority header differs from schema")
    rows = read_tsv(CHOICE_RESOLUTION_PATH)
    exact_by_contract = {row["contract_id"]: row for row in exact_rows}
    choice_bundles: dict[
        tuple[str, str], dict[str, set[str]]
    ] = defaultdict(lambda: defaultdict(set))
    for authority in outcome_authority_rows:
        if authority["disposition"] == "REQUIRE_ONE_OF":
            choice_bundles[
                (authority["contract_id"], authority["choice_set_id"])
            ][authority["choice_alternative_id"]].add(authority["route_id"])
    for contract_id, _ in choice_bundles:
        if exact_by_contract[contract_id]["policy_variant_id"] != "COMMON-NON-OD1":
            raise VerificationError(
                "the frozen choice selector omits OD-1, but a choice-bearing "
                f"contract is OD-1-specific: {contract_id}"
            )

    by_id: dict[str, dict[str, str]] = {}
    previous_id = ""
    for row in rows:
        resolution_id = row["choice_resolution_id"]
        if resolution_id in by_id:
            raise VerificationError(f"duplicate choice resolution ID {resolution_id}")
        if previous_id and resolution_id <= previous_id:
            raise VerificationError("choice resolutions must sort by choice_resolution_id")
        previous_id = resolution_id
        if row["schema_version"] != CHOICE_RESOLUTION_SCHEMA_VERSION:
            raise VerificationError("choice resolution schema changed")
        exact = exact_by_contract.get(row["contract_id"])
        if exact is None:
            raise VerificationError(
                f"choice resolution names unknown contract {row['contract_id']}"
            )
        if row["member_contract_id"] != exact["member_contract_id"] or row[
            "outcome_id"
        ] != exact["outcome_id"]:
            raise VerificationError(
                f"choice resolution source mismatch for {resolution_id}"
            )
        choice_key = (row["contract_id"], row["choice_set_id"])
        alternatives = choice_bundles.get(choice_key)
        if alternatives is None or row["selected_alternative_id"] not in alternatives:
            raise VerificationError(
                f"choice resolution selects an unknown choice set or alternative for {resolution_id}"
            )
        if row["payload_branch_id"] in {"", "ALL"}:
            raise VerificationError(
                f"choice resolution requires an exact payload branch for {resolution_id}"
            )
        if any(row[field] == "" for field in CHOICE_RESOLUTION_SELECTOR_FIELDS):
            raise VerificationError(f"choice resolution has an empty selector for {resolution_id}")
        if not choice_resolution_selected_bundle_is_exact(row, choice_bundles):
            raise VerificationError(
                f"choice resolution does not name the complete selected bundle for {resolution_id}"
            )
        if row["selection_basis"] in {"", "NONE"}:
            raise VerificationError(f"choice resolution is unanchored for {resolution_id}")
        if row["authority_status"] != OUTCOME_AUTHORITY_STATUS:
            raise VerificationError(f"choice resolution status changed for {resolution_id}")
        selector_material = "\t".join(
            (
                row["contract_id"],
                row["choice_set_id"],
                row["payload_branch_id"],
                row["payload_size_class"],
                row["stored_borrow_route_id"],
                row["od3_policy_variant_id"],
                row["od4_policy_option"],
            )
        )
        expected_id = (
            "CHOICE-RESOLUTION::"
            + hashlib.sha256(selector_material.encode()).hexdigest()
        )
        if resolution_id != expected_id:
            raise VerificationError(f"choice resolution ID mismatch for {resolution_id}")
        expected_hash = row_sha256(
            row,
            [
                field
                for field in CHOICE_RESOLUTION_FIELDS
                if field != "choice_resolution_sha256"
            ],
        )
        if row["choice_resolution_sha256"] != expected_hash:
            raise VerificationError(f"choice resolution hash mismatch for {resolution_id}")
        by_id[resolution_id] = row

    by_context_choice, used_ids = validate_choice_resolution_coverage(
        contexts, choice_bundles, rows
    )
    if used_ids != set(by_id):
        raise VerificationError(
            "choice resolution registry contains unused or uncovered selector rows"
        )
    return rows, by_context_choice


def independent_allocator_obligation(
    exact: dict[str, str], context: dict[str, str], allocator_capable: set[str]
) -> str:
    if context["context_kind"] in {
        "SOURCE_NEGATIVE",
        "OD3_DEFER_ZST_REJECTION",
        "OD3_INCLUDE_ZST_OUTCOME_INAPPLICABLE",
    }:
        return "FORBID_ACTIVATED"
    if context["payload_size_class"] == "ZERO_SIZE":
        return "FORBID_ACTIVATED"
    suffix = outcome_suffix(exact)
    if suffix in {"OOM_ABORT", "ALLOCATION_ERROR_RETURN", "SUCCESS_GROW", "SUCCESS_RELOCATE"}:
        return "REQUIRE_ACTIVATED"
    if suffix in {
        "SUCCESS_NO_GROW",
        "SUCCESS_NO_CHANGE",
        "CAPACITY_OVERFLOW_TRAP",
        "CAPACITY_ERROR_RETURN",
        "BOUNDS_TRAP",
        "PRECONDITION_TRAP",
        "CHECKED_ERROR",
        "OVERFILL_REJECTED",
        "UNDERFILL_CLOSE_REJECTED",
    }:
        return "FORBID_ACTIVATED"
    if exact["member_contract_id"] not in allocator_capable:
        if "OD-4" in exact["status"]:
            return "REQUIRE_UNRESOLVED"
        return "FORBID_ACTIVATED"
    return "REQUIRE_UNRESOLVED"


def render_route_obligations(
    exact_rows: list[dict[str, str]],
    contexts: list[dict[str, str]],
    routes: dict[str, dict[str, str]],
    allocator_capable: set[str],
    outcome_authority_by_key: dict[tuple[str, str], dict[str, str]],
    predicate_authority_by_id: dict[str, dict[str, str]],
    exact_route_evidence_by_key: dict[tuple[str, str], dict[str, str]],
    route_multiplicity_by_key: dict[tuple[str, str], dict[str, str]],
    choice_resolution_by_context: dict[tuple[str, str], dict[str, str]],
) -> list[dict[str, str]]:
    exact_by_contract = {row["contract_id"]: row for row in exact_rows}
    premise_authority = read_premise_authority(exact_rows, routes)
    result: list[dict[str, str]] = []
    obligation_keys: set[tuple[str, str]] = set()

    def add(
        context: dict[str, str],
        exact: dict[str, str],
        kind: str,
        route_id: str,
        authority_kind: str,
        detail: str,
        *,
        choice_set_id: str = "NONE",
        choice_alternative_id: str = "NONE",
        predicate_id: str = "NONE",
        predicate_evaluation: str = "NOT_APPLICABLE",
        blocker_ids: str = "NONE",
        outcome_authority_entry_id: str = "NONE",
        outcome_authority_entry_sha256: str = "NONE",
        predicate_authority_sha256: str = "NONE",
        exact_route_evidence_id: str = "NONE",
        exact_route_evidence_sha256: str = "NONE",
        choice_resolution_id: str = "NONE",
        choice_resolution_sha256: str = "NONE",
        minimum_route_invocation_count: str = "0",
        route_multiplicity_authority_id: str = "NONE",
        route_multiplicity_authority_sha256: str = "NONE",
    ) -> None:
        material = "\t".join(
            (
                context["context_id"],
                kind,
                route_id,
                authority_kind,
                detail,
                choice_set_id,
                choice_alternative_id,
                predicate_id,
                predicate_evaluation,
                blocker_ids,
                outcome_authority_entry_id,
                outcome_authority_entry_sha256,
                predicate_authority_sha256,
                exact_route_evidence_id,
                exact_route_evidence_sha256,
                choice_resolution_id,
                choice_resolution_sha256,
                minimum_route_invocation_count,
                route_multiplicity_authority_id,
                route_multiplicity_authority_sha256,
            )
        )
        row = {
            "schema_version": SCHEMA_VERSION,
            "obligation_id": f"ROUTE-OBLIGATION::{hashlib.sha256(material.encode()).hexdigest()}",
            "context_id": context["context_id"],
            "contract_id": exact["contract_id"],
            "member_contract_id": exact["member_contract_id"],
            "obligation_kind": kind,
            "route_id": route_id,
            "authority_kind": authority_kind,
            "authority_detail": detail,
            "choice_set_id": choice_set_id,
            "choice_alternative_id": choice_alternative_id,
            "predicate_id": predicate_id,
            "predicate_evaluation": predicate_evaluation,
            "blocker_ids": blocker_ids,
            "outcome_authority_entry_id": outcome_authority_entry_id,
            "outcome_authority_entry_sha256": outcome_authority_entry_sha256,
            "predicate_authority_sha256": predicate_authority_sha256,
            "exact_route_evidence_id": exact_route_evidence_id,
            "exact_route_evidence_sha256": exact_route_evidence_sha256,
            "choice_resolution_id": choice_resolution_id,
            "choice_resolution_sha256": choice_resolution_sha256,
            "minimum_route_invocation_count": minimum_route_invocation_count,
            "route_multiplicity_authority_id": route_multiplicity_authority_id,
            "route_multiplicity_authority_sha256": route_multiplicity_authority_sha256,
            "source_status": exact["status"],
            "obligation_row_sha256": "",
        }
        row["obligation_row_sha256"] = row_sha256(
            row,
            [
                field
                for field in ROUTE_OBLIGATION_FIELDS
                if field != "obligation_row_sha256"
            ],
        )
        key = (context["context_id"], route_id)
        if key in obligation_keys:
            raise VerificationError(
                f"duplicate canonical route obligation for {key} from {authority_kind}"
            )
        obligation_keys.add(key)
        result.append(row)

    positive_route_ids = {
        route_id
        for route_id, route in routes.items()
        if route["route_class"] != "NEGATIVE_DISPOSITION"
    }
    callable_routes = {
        "CALLABLE-CLONE",
        "CALLABLE-PRODUCER",
        "CALLABLE-DEFAULT",
        "CALLABLE-USER",
    }
    for context in contexts:
        exact = exact_by_contract[context["contract_id"]]
        for blocker_route in split_ids(context["source_blocker_route_ids"]):
            if (
                context["context_kind"] == "SOURCE_NEGATIVE"
                and blocker_route == context["od3_route_id"]
            ):
                continue
            add(
                context,
                exact,
                "REQUIRE_ACTIVATED",
                blocker_route,
                "EXACT_SOURCE_STATUS",
                exact["status"],
            )
        if context["context_kind"] == "SOURCE_NEGATIVE":
            add(
                context,
                exact,
                "REQUIRE_ACTIVATED",
                context["od3_route_id"],
                "EXACT_NEGATIVE_MEMBER",
                exact["member_contract_id"],
            )
            for route_id in sorted(positive_route_ids):
                add(
                    context,
                    exact,
                    "FORBID_ACTIVATED",
                    route_id,
                    "SOURCE_NEGATIVE_CLOSURE",
                    exact["status"],
                )
            continue
        if context["context_kind"] == "OD3_DEFER_ZST_REJECTION":
            add(
                context,
                exact,
                "REQUIRE_ACTIVATED",
                "NEG-STATIC-REJECTION",
                "OD3_POLICY_AND_SIZE",
                "OD-3-DEFER-ZST::ZERO_SIZE",
            )
            for route_id in sorted(positive_route_ids):
                add(
                    context,
                    exact,
                    "FORBID_ACTIVATED",
                    route_id,
                    "OD3_ZST_REJECTION_CLOSURE",
                    "OD-3-DEFER-ZST::ZERO_SIZE",
                )
            continue
        if context["context_kind"] == "OD3_INCLUDE_ZST_OUTCOME_INAPPLICABLE":
            add(
                context,
                exact,
                "REQUIRE_ACTIVATED",
                "NEG-INAPPLICABLE-CONTEXT",
                "OD3_POLICY_SIZE_AND_EXACT_OUTCOME",
                outcome_suffix(exact),
            )
            for route_id in sorted(positive_route_ids):
                add(
                    context,
                    exact,
                    "FORBID_ACTIVATED",
                    route_id,
                    "OD3_INAPPLICABLE_OUTCOME_CLOSURE",
                    "OD-3-INCLUDE-ZST::ZERO_SIZE",
                )
            continue

        for route_id in sorted(OUTCOME_AUTHORITY_ROUTE_IDS):
            authority = outcome_authority_by_key[(exact["contract_id"], route_id)]
            exact_route_evidence = exact_route_evidence_by_key.get(
                (exact["contract_id"], route_id)
            )
            route_multiplicity = route_multiplicity_by_key.get(
                (exact["contract_id"], route_id)
            )
            kind = {
                "REQUIRED": "REQUIRE_ACTIVATED",
                "FORBIDDEN": "FORBID_ACTIVATED",
                "CONDITIONAL_WITH_FROZEN_PREDICATE": "CONDITIONAL_AVAILABLE_NO_CREDIT",
                "REQUIRE_ONE_OF": "REQUIRE_ONE_OF_NO_CREDIT",
                "UNRESOLVED_WITH_BLOCKER": "REQUIRE_UNRESOLVED",
            }[authority["disposition"]]
            predicate_evaluation = "NOT_APPLICABLE"
            if authority["disposition"] == "CONDITIONAL_WITH_FROZEN_PREDICATE":
                predicate = predicate_authority_by_id[authority["predicate_id"]]
                predicate_evaluation = evaluate_frozen_predicate(
                    predicate, exact, context
                )
                if predicate_evaluation in {"TRUE", "FALSE"}:
                    selected_disposition = predicate[
                        "true_route_disposition"
                        if predicate_evaluation == "TRUE"
                        else "false_route_disposition"
                    ]
                    kind = {
                        "REQUIRED": "REQUIRE_ACTIVATED",
                        "FORBIDDEN": "FORBID_ACTIVATED",
                    }[selected_disposition]
            choice_resolution = None
            if authority["disposition"] == "REQUIRE_ONE_OF":
                choice_resolution = choice_resolution_by_context[
                    (context["context_id"], authority["choice_set_id"])
                ]
                kind = (
                    "REQUIRE_ACTIVATED"
                    if authority["choice_alternative_id"]
                    == choice_resolution["selected_alternative_id"]
                    else "FORBID_ACTIVATED"
                )
            add(
                context,
                exact,
                kind,
                route_id,
                "EXACT_OUTCOME_ROUTE_AUTHORITY",
                authority["authority_basis"],
                choice_set_id=authority["choice_set_id"],
                choice_alternative_id=authority["choice_alternative_id"],
                predicate_id=authority["predicate_id"],
                predicate_evaluation=predicate_evaluation,
                blocker_ids=authority["blocker_ids"],
                outcome_authority_entry_id=authority["authority_entry_id"],
                outcome_authority_entry_sha256=authority[
                    "authority_entry_sha256"
                ],
                predicate_authority_sha256=(
                    predicate_authority_by_id[authority["predicate_id"]][
                        "predicate_sha256"
                    ]
                    if authority["predicate_id"] != "NONE"
                    else "NONE"
                ),
                exact_route_evidence_id=(
                    exact_route_evidence["route_evidence_id"]
                    if exact_route_evidence
                    else "NONE"
                ),
                exact_route_evidence_sha256=(
                    exact_route_evidence["route_evidence_sha256"]
                    if exact_route_evidence
                    else "NONE"
                ),
                choice_resolution_id=(
                    choice_resolution["choice_resolution_id"]
                    if choice_resolution
                    else "NONE"
                ),
                choice_resolution_sha256=(
                    choice_resolution["choice_resolution_sha256"]
                    if choice_resolution
                    else "NONE"
                ),
                minimum_route_invocation_count=(
                    route_multiplicity["minimum_invocation_count"]
                    if route_multiplicity
                    else "0"
                ),
                route_multiplicity_authority_id=(
                    route_multiplicity["multiplicity_authority_id"]
                    if route_multiplicity
                    else "NONE"
                ),
                route_multiplicity_authority_sha256=(
                    route_multiplicity["multiplicity_authority_sha256"]
                    if route_multiplicity
                    else "NONE"
                ),
            )

        add(
            context,
            exact,
            "REQUIRE_ACTIVATED",
            exit_route(exact),
            "EXACT_OUTCOME_EXIT",
            exact["commit_phase"],
        )
        add(
            context,
            exact,
            "REQUIRE_ACTIVATED",
            "PROTOCOL-OPAQUE-OWNER",
            "EXACT_OWNER_ROLE",
            exact["owner_role_foreign_key"],
        )
        for route_id in split_ids(context["payload_overlay_route_ids"]):
            if route_id == "PROTOCOL-STORED-BORROW":
                if context["stored_borrow_route_id"] != "NONE":
                    add(
                        context,
                        exact,
                        "REQUIRE_ACTIVATED",
                        route_id,
                        "EXACT_STORED_BORROW_SELECTOR",
                        context["stored_borrow_route_id"],
                    )
                continue
            if route_id in {"FACT-BORROW", "FACT-STATE"} and outcome_path_kind(exact) != "NORMAL":
                continue
            add(
                context,
                exact,
                "REQUIRE_ACTIVATED",
                route_id,
                "AUTHORITY_MEMBER_OR_OVERLAY_NORMAL_TRANSITION",
                context["payload_branch_id"],
            )
        if context["payload_size_class"] == "ZERO_SIZE":
            add(
                context,
                exact,
                "REQUIRE_ACTIVATED",
                "CORE-ZST-LOGICAL",
                "OD3_POLICY_AND_SIZE",
                "OD-3-INCLUDE-ZST::ZERO_SIZE",
            )

        member = exact["member_contract_id"]
        authority = premise_authority[member]
        if outcome_path_kind(exact) == "NORMAL":
            for route_id in split_ids(authority["required_normal_route_ids"]):
                if route_id in OUTCOME_AUTHORITY_ROUTE_IDS:
                    continue
                add(
                    context,
                    exact,
                    "REQUIRE_ACTIVATED",
                    route_id,
                    "INDEPENDENT_MEMBER_PREMISE_AUTHORITY",
                    authority["authority_id"],
                )
        for route_id in split_ids(authority["required_protocol_route_ids"]):
            if route_id in OUTCOME_AUTHORITY_ROUTE_IDS:
                continue
            add(
                context,
                exact,
                "REQUIRE_ACTIVATED",
                route_id,
                "INDEPENDENT_MEMBER_PREMISE_AUTHORITY",
                authority["authority_id"],
            )

        expected_behavior = independent_behavior_route(exact, premise_authority)
        if expected_behavior == "NONE":
            for route_id in sorted(callable_routes):
                add(
                    context,
                    exact,
                    "FORBID_ACTIVATED",
                    route_id,
                    "EXACT_ZERO_BEHAVIOR_CALLS",
                    exact["behavior_calls"],
                )
        elif member == "DENSE-CONVERT":
            add(
                context,
                exact,
                "REQUIRE_UNRESOLVED",
                expected_behavior,
                "EXACT_COARSE_CONVERSION_BEHAVIOR",
                exact["behavior_calls"],
                blocker_ids="CONVERT-DIRECTION-SUBCONTRACTS-NOT-SPLIT",
            )
        else:
            add(
                context,
                exact,
                "REQUIRE_ACTIVATED",
                expected_behavior,
                "EXACT_BEHAVIOR_CALL_CONTRACT",
                exact["behavior_calls"],
            )

        allocator_obligation = independent_allocator_obligation(
            exact, context, allocator_capable
        )
        add(
            context,
            exact,
            allocator_obligation,
            "FAIL-ALLOC-EXACT",
            "STRUCTURED_ALLOCATION_OUTCOME",
            "|".join(
                (
                    outcome_suffix(exact),
                    exact["allocation_before"],
                    exact["allocation_after"],
                    exact["trigger"],
                )
            ),
        )
        od1 = od1_authority_by_key().get(
            (exact["policy_variant_id"], exact["member_contract_id"])
        )
        if od1 and od1["member_class"] == "GROWTH_CAPABLE_MUTATOR":
            add(
                context,
                exact,
                "REQUIRE_ACTIVATED",
                "FAIL-RESERVE-FIRST",
                "OD1_POLICY_REGISTRY",
                "|".join(
                    (
                        od1["last_recoverable_point"],
                        od1["first_destructive_commit"],
                        od1["default_path_result_branch"],
                        od1["unknown_length_rule"],
                    )
                ),
            )
    result.sort(key=lambda row: row["obligation_id"])
    return result


def evaluate_route_obligations(
    contexts: list[dict[str, str]],
    action_steps: list[dict[str, str]],
    obligations: list[dict[str, str]],
) -> dict[str, int]:
    steps_by_key: dict[tuple[str, str], dict[str, str]] = {}
    for step in action_steps:
        key = (step["context_id"], step["route_id"])
        if key in steps_by_key:
            raise VerificationError(f"duplicate action-step route key {key}")
        steps_by_key[key] = step
    missing_by_context: Counter[str] = Counter()
    violations_by_context: Counter[str] = Counter()
    unresolved_by_context: Counter[str] = Counter()
    conditional_by_context: Counter[str] = Counter()
    runtime_branch_conditional_by_context: Counter[str] = Counter()
    trace_classifier_conditional_by_context: Counter[str] = Counter()
    choice_by_context: Counter[str] = Counter()
    required = satisfied = forbidden = forbidden_violations = unresolved = 0
    conditional = runtime_branch_conditional = trace_classifier_conditional = choice = 0
    seen_obligation_keys: set[tuple[str, str]] = set()
    for obligation in obligations:
        key = (obligation["context_id"], obligation["route_id"])
        if key in seen_obligation_keys:
            raise VerificationError(f"duplicate canonical route obligation {key}")
        seen_obligation_keys.add(key)
        step = steps_by_key.get(key)
        activated = bool(step) and route_applicability_is_activated(
            step["route_applicability"]
        )
        is_unresolved = bool(step) and step["route_applicability"].startswith(
            "UNRESOLVED"
        )
        kind = obligation["obligation_kind"]
        if kind == "REQUIRE_ACTIVATED":
            required += 1
            if activated:
                satisfied += 1
            else:
                missing_by_context[obligation["context_id"]] += 1
        elif kind == "FORBID_ACTIVATED":
            forbidden += 1
            if activated:
                forbidden_violations += 1
                violations_by_context[obligation["context_id"]] += 1
        elif kind == "REQUIRE_UNRESOLVED":
            if is_unresolved:
                unresolved += 1
                unresolved_by_context[obligation["context_id"]] += 1
            else:
                missing_by_context[obligation["context_id"]] += 1
        elif kind == "CONDITIONAL_AVAILABLE_NO_CREDIT":
            conditional += 1
            guard_kind = FROZEN_OUTCOME_PREDICATES[
                obligation["predicate_id"]
            ][0]
            if guard_kind == "TRACE_CLASSIFIER_ONLY":
                trace_classifier_conditional += 1
                trace_classifier_conditional_by_context[
                    obligation["context_id"]
                ] += 1
            else:
                runtime_branch_conditional += 1
                runtime_branch_conditional_by_context[
                    obligation["context_id"]
                ] += 1
            if (
                step
                and step["route_applicability"].startswith("CONDITIONAL_")
                and obligation["predicate_id"] != "NONE"
                and obligation["predicate_evaluation"]
                in UNOBSERVED_OUTCOME_PREDICATE_EVALUATIONS
            ):
                conditional_by_context[obligation["context_id"]] += 1
            else:
                missing_by_context[obligation["context_id"]] += 1
        elif kind == "REQUIRE_ONE_OF_NO_CREDIT":
            choice += 1
            if step and step["route_applicability"].startswith("CHOICE_"):
                choice_by_context[obligation["context_id"]] += 1
            else:
                missing_by_context[obligation["context_id"]] += 1
        else:
            raise VerificationError(f"unknown route obligation kind {kind}")

    for context in contexts:
        missing = missing_by_context[context["context_id"]]
        contradictions = violations_by_context[context["context_id"]]
        if missing:
            context["semantic_route_status"] = "INDEPENDENT_REQUIRED_ROUTE_GAP"
        elif contradictions:
            context["semantic_route_status"] = "INDEPENDENT_FORBIDDEN_ROUTE_CONTRADICTION"
        elif unresolved_by_context[context["context_id"]] or choice_by_context[
            context["context_id"]
        ]:
            context[
                "semantic_route_status"
            ] = "INDEPENDENT_OBLIGATION_SET_CLASSIFIED_WITH_EXPLICIT_BLOCKERS_NO_POSITIVE_CREDIT"
        elif conditional_by_context[context["context_id"]]:
            context[
                "semantic_route_status"
            ] = "INDEPENDENT_OBLIGATION_SET_CONDITIONAL_PARTITION_CLOSED_RUNTIME_ACTION_UNOBSERVED"
        else:
            context["semantic_route_status"] = "INDEPENDENT_OBLIGATION_SET_CLOSED"
        context["context_row_sha256"] = row_sha256(
            context,
            [
                field
                for field in BRANCH_CONTEXT_FIELDS
                if field != "context_row_sha256"
            ],
        )
    return {
        "required_route_obligations": required,
        "satisfied_required_route_obligations": satisfied,
        "missing_required_route_obligations": sum(missing_by_context.values()),
        "forbidden_route_obligations": forbidden,
        "forbidden_route_violations": forbidden_violations,
        "unresolved_route_applicability_contexts": len(
            {
                obligation["context_id"]
                for obligation in obligations
                if obligation["obligation_kind"] == "REQUIRE_UNRESOLVED"
            }
        ),
        "unresolved_route_obligations": unresolved,
        "conditional_route_availability_obligations": conditional,
        "conditional_partition_closed_obligations": conditional,
        "runtime_conditional_action_unobserved_obligations": conditional,
        "runtime_branch_conditional_obligations": runtime_branch_conditional,
        "trace_classifier_conditional_obligations": trace_classifier_conditional,
        "trace_classifier_action_unobserved_obligations": trace_classifier_conditional,
        "conditional_partition_closed_contexts": len(conditional_by_context),
        "runtime_conditional_action_unobserved_contexts": len(
            conditional_by_context
        ),
        "runtime_conditional_cost_unobserved_contexts": len(
            conditional_by_context
        ),
        "runtime_branch_conditional_contexts": len(
            runtime_branch_conditional_by_context
        ),
        "trace_classifier_conditional_contexts": len(
            trace_classifier_conditional_by_context
        ),
        "trace_classifier_cost_unobserved_contexts": len(
            trace_classifier_conditional_by_context
        ),
        "choice_route_availability_obligations": choice,
        "classified_route_obligations": len(obligations),
    }


def context_allocator_mode(
    context: dict[str, str], exact: dict[str, str], allocator_capable: set[str]
) -> str:
    if context["context_kind"] in {
        "SOURCE_NEGATIVE",
        "OD3_DEFER_ZST_REJECTION",
        "OD3_INCLUDE_ZST_OUTCOME_INAPPLICABLE",
    }:
        return "FORBIDDEN_BY_CONTEXT_POLICY"
    if context["payload_size_class"] == "ZERO_SIZE":
        return "FORBIDDEN_FOR_ZST_PAYLOAD_STORAGE"
    return outer_allocator_route_applicability(exact, allocator_capable)


def render_structural_costs(
    exact_rows: list[dict[str, str]],
    contexts: list[dict[str, str]],
    action_steps: list[dict[str, str]],
    allocator_capable: set[str],
    route_multiplicity_by_key: dict[tuple[str, str], dict[str, str]],
) -> list[dict[str, str]]:
    exact_by_contract = {row["contract_id"]: row for row in exact_rows}
    groups = member_group_map()
    od4_rows = {row["policy_variant_id"]: row for row in read_tsv(OD4_PATH)}
    steps_by_context: dict[str, list[dict[str, str]]] = defaultdict(list)
    for step in action_steps:
        steps_by_context[step["context_id"]].append(step)
    result: list[dict[str, str]] = []
    exact_sha = sha256_path(EXACT_PATH)
    for context in contexts:
        exact = exact_by_contract[context["contract_id"]]
        context_steps = steps_by_context[context["context_id"]]
        premise, behavior_route = behavior_premise(
            exact["member_contract_id"], groups[exact["member_contract_id"]]
        )
        allocator_mode = context_allocator_mode(context, exact, allocator_capable)
        behavior_mode = behavior_route_applicability(exact, behavior_route)
        if context["context_kind"] in {
            "SOURCE_NEGATIVE",
            "OD3_DEFER_ZST_REJECTION",
            "OD3_INCLUDE_ZST_OUTCOME_INAPPLICABLE",
        }:
            behavior_mode = "FORBIDDEN_BY_CONTEXT_POLICY"
        if context["context_kind"] == "SOURCE_NEGATIVE":
            od3_cost = "SOURCE_NEGATIVE_NO_RUNTIME_ROUTE"
        elif context["context_kind"] == "OD3_DEFER_ZST_REJECTION":
            od3_cost = "STATIC_REJECTION_ZERO_RUNTIME"
        elif context["context_kind"] == "OD3_INCLUDE_ZST_OUTCOME_INAPPLICABLE":
            od3_cost = "ZST_ALLOCATOR_OUTCOME_INAPPLICABLE_ZERO_RUNTIME"
        elif context["payload_size_class"] == "POSITIVE_SIZE":
            od3_cost = "POSITIVE_SIZE_SOURCE_CEILING_APPLIES"
        else:
            od3_cost = "ZERO_PAYLOAD_BYTES_ZERO_PAYLOAD_ALLOCATOR_LOGICAL_TRANSFERS_RETAINED"
        od4_allocation = (
            "NOT_APPLICABLE"
            if context["od4_policy_option"] == "NOT_APPLICABLE"
            else od4_rows[context["od4_policy_option"]]["allocation_contract"]
        )
        stored_cost = (
            "NONE"
            if context["stored_borrow_route_id"] == "NONE"
            else "EXACT_STORED_ROUTE_REGION_FREE_ZERO_TAX_CLAIM_UNMEASURED"
        )
        multiplicity_rows = (
            sorted(
                (
                    row
                    for (contract_id, _), row in route_multiplicity_by_key.items()
                    if contract_id == exact["contract_id"]
                ),
                key=lambda row: row["route_id"],
            )
            if context["context_kind"]
            not in {
                "SOURCE_NEGATIVE",
                "OD3_DEFER_ZST_REJECTION",
                "OD3_INCLUDE_ZST_OUTCOME_INAPPLICABLE",
            }
            else []
        )
        route_multiplicity_lower_bounds = join_ids(
            [
                f"{row['route_id']}>={row['minimum_invocation_count']}"
                for row in multiplicity_rows
            ]
        )
        route_multiplicity_authority_ids = join_ids(
            [row["multiplicity_authority_id"] for row in multiplicity_rows]
        )
        route_multiplicity_authority_sha256s = join_ids(
            [row["multiplicity_authority_sha256"] for row in multiplicity_rows]
        )
        route_multiplicity_evidence_status = (
            "EXACT_STRUCTURAL_LOWER_BOUND_UNMEASURED_NO_P1_CREDIT"
            if multiplicity_rows
            else "NONE"
        )
        route_vector = [
            f"{step['route_id']}={step['route_applicability']}::{step['symbolic_cost_delta']}"
            for step in context_steps
        ]
        vector_material = "\n".join(
            [
                exact["resource_ceiling"],
                allocator_mode,
                behavior_mode,
                od3_cost,
                od4_allocation,
                stored_cost,
                route_multiplicity_lower_bounds,
            ]
            + route_vector
        )
        if any(
            step["route_applicability"].startswith("CONDITIONAL_")
            for step in context_steps
        ):
            route_cost_status = (
                "PAPER_CONDITIONAL_PARTITION_STRUCTURAL_CEILING_COVERED_"
                "RUNTIME_COST_UNOBSERVED"
            )
        elif context["source_status"] == "REQUIRED_IN_LOCK":
            route_cost_status = "PAPER_ROUTE_LEVEL_STRUCTURAL_CEILING_COVERED"
        elif context["source_status"] == "EXCLUDED_BLOCKS_NAMED_CLAIM":
            route_cost_status = "NEGATIVE_ROUTE_ZERO_POSITIVE_CREDIT_COVERED"
        else:
            route_cost_status = "SOURCE_BLOCKER_PRESERVED_ROUTE_LEVEL_CEILING_COVERED"
        blockers = {"MECHANIZED-COST-ALGEBRA-NOT-PRODUCED", "PERFORMANCE-NOT-AUTHORIZED"}
        if any(
            "TRACE-CLASSIFIER-NOT-OBSERVED-NO-ACTION-OR-COST-CREDIT"
            in step["step_blocker_ids"]
            for step in context_steps
        ):
            blockers.add("TRACE-CLASSIFIER-NOT-OBSERVED-NO-COST-CREDIT")
        if exact["member_contract_id"] == "DENSE-ROTATE":
            blockers.add("ROTATE_OPTIMAL_DISPATCH_NOT_MEASURED")
        if multiplicity_rows:
            blockers.add("ROUTE-MULTIPLICITY-MEASURED-COST-NOT-AUTHORIZED")
        if context["od4_policy_option"] != "NOT_APPLICABLE":
            blockers.add("OD-4")
        if "UNKNOWN_LENGTH" in context["source_status"]:
            blockers.add("UNKNOWN-LENGTH-RECOVERY")
        row = {
            "schema_version": SCHEMA_VERSION,
            "cost_evidence_id": context["cost_evidence_id"],
            "context_id": context["context_id"],
            "contract_id": exact["contract_id"],
            "member_contract_id": exact["member_contract_id"],
            "outcome_id": exact["outcome_id"],
            "source_status": exact["status"],
            "action_class_id": groups[exact["member_contract_id"]],
            "context_cost_variant_id": hashlib.sha256(
                "\t".join(
                    (
                        exact["contract_id"],
                        context["payload_branch_id"],
                        context["payload_size_class"],
                        context["stored_borrow_route_id"],
                        context["od3_policy_variant_id"],
                        context["od4_policy_option"],
                    )
                ).encode()
            ).hexdigest(),
            "resource_ceiling": exact["resource_ceiling"],
            "source_ceiling_sha256": hashlib.sha256(exact["resource_ceiling"].encode()).hexdigest(),
            "outer_allocator_route_applicability": allocator_mode,
            "behavior_route_applicability": behavior_mode,
            "od3_cost_disposition": od3_cost,
            "od4_allocation_contract": od4_allocation,
            "stored_borrow_cost_disposition": stored_cost,
            "zero_payload_traffic_required": (
                "YES_STATIC_NO_RUNTIME"
                if context["context_kind"] in {
                    "SOURCE_NEGATIVE",
                    "OD3_DEFER_ZST_REJECTION",
                    "OD3_INCLUDE_ZST_OUTCOME_INAPPLICABLE",
                }
                else (
                    "YES_ZERO_PHYSICAL_BYTES_LOGICAL_TRANSFERS_RETAINED"
                    if context["payload_size_class"] == "ZERO_SIZE"
                    else zero_payload_traffic_required(exact)
                )
            ),
            "zero_allocation_required": (
                "YES_CONTEXT_POLICY"
                if allocator_mode.startswith("FORBIDDEN")
                else zero_outer_allocation_required(exact, allocator_capable)
            ),
            "behavior_premise_kind": premise,
            "exact_behavior_calls": exact["behavior_calls"],
            "exact_commitment_point": exact["commitment_point"],
            "exact_cleanup": exact["normal_exit_cleanup"],
            "route_multiplicity_lower_bounds": route_multiplicity_lower_bounds,
            "route_multiplicity_authority_ids": route_multiplicity_authority_ids,
            "route_multiplicity_authority_sha256s": route_multiplicity_authority_sha256s,
            "route_multiplicity_evidence_status": route_multiplicity_evidence_status,
            "normalized_symbolic_vector_sha256": hashlib.sha256(vector_material.encode()).hexdigest(),
            "route_level_structural_status": route_cost_status,
            "mechanized_cost_algebra_status": "NOT_PRODUCED_SOURCE_CEILING_AND_ROUTE_RULES_NOT_NORMALIZED_TO_ALGEBRA",
            "measured_status": "NOT_AUTHORIZED_NOT_RUN",
            "protected_control_ids": "B-FIX,B-P2",
            "protected_control_status": "PENDING_EXTERNAL_NO_PASS_CREDIT",
            "blocker_ids": join_ids(blockers),
            "exact_registry_sha256": exact_sha,
            "structural_cost_row_sha256": "",
        }
        row["structural_cost_row_sha256"] = row_sha256(
            row, [field for field in STRUCTURAL_COST_FIELDS if field != "structural_cost_row_sha256"]
        )
        result.append(row)
    return result


def source_discrepancy_counts(
    exact_rows: list[dict[str, str]],
    authority_rows: list[dict[str, str]],
    contexts: list[dict[str, str]],
) -> tuple[int, int, int]:
    required_by_member: dict[str, set[str]] = defaultdict(set)
    for row in authority_rows:
        if row["binding_kind"] == "MEMBER" and row["applicability"] == "REQUIRED":
            required_by_member[row["member_contract_id"]].add(row["capability_id"])
    omissions = 0
    legacy_only = 0
    missing_by_contract: dict[str, set[str]] = {}
    for exact in exact_rows:
        legacy = set(split_ids(exact["capability_ids"]))
        required = required_by_member[exact["member_contract_id"]]
        missing = required - legacy
        missing_by_contract[exact["contract_id"]] = missing
        omissions += len(missing)
        legacy_only += len(legacy - required)
    context_omissions = sum(
        len(missing_by_contract[context["contract_id"]]) for context in contexts
    )
    return omissions, context_omissions, legacy_only


