    if mutation_obligation_failures(steps, obligations) != (0, 0):
        raise VerificationError("hostile baseline route obligations are not closed")
    if mutation_context_relation_errors(exact_rows, contexts) != 0:
        raise VerificationError("hostile baseline context product is not exact")
    if mutation_discharge_mismatches(contexts, steps, discharges, routes) != 0:
        raise VerificationError("hostile baseline discharge relation is inconsistent")
    if mutation_obligation_relation_errors(expected_obligations, obligations) != 0:
        raise VerificationError("hostile baseline obligation relation is not exact")
    if member_authority_mismatch_count(members, premise_authority) != 0:
        raise VerificationError("hostile baseline premise authority differs from classifier")

    deleted_multiplicity_rows = [dict(row) for row in route_multiplicity_rows[1:]]
