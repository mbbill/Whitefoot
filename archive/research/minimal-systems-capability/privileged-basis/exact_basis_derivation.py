def validate_independent_outcome_route_sentinels(
    exact_rows: list[dict[str, str]],
    outcome_authority_by_key: dict[tuple[str, str], dict[str, str]],
) -> None:
    sentinels_by_selector: dict[
        tuple[str, str, str], tuple[str, str]
    ] = {}
    for member_contract_id, suffix, route_id, disposition, predicate_id in (
        INDEPENDENT_OUTCOME_ROUTE_SENTINELS
    ):
        selector = (member_contract_id, suffix, route_id)
        if selector in sentinels_by_selector:
            raise VerificationError(
                f"duplicate independent outcome-route sentinel {selector}"
            )
        if route_id not in OUTCOME_AUTHORITY_ROUTE_IDS:
            raise VerificationError(
                f"independent outcome-route sentinel names unknown route {route_id}"
            )
        if disposition not in OUTCOME_AUTHORITY_DISPOSITIONS:
            raise VerificationError(
                f"independent outcome-route sentinel names unknown disposition {disposition}"
            )
        if disposition == OUTCOME_ROUTE_SENTINEL_CONDITIONAL:
            if predicate_id not in FROZEN_OUTCOME_PREDICATES:
                raise VerificationError(
                    "independent conditional outcome-route sentinel names an "
                    f"unknown predicate {predicate_id}"
                )
        elif predicate_id != "NONE":
            raise VerificationError(
                "independent nonconditional outcome-route sentinel carries a predicate"
            )
        sentinels_by_selector[selector] = (disposition, predicate_id)

    if not set(INDEPENDENT_OUTCOME_ROUTE_BLOCKER_SENTINELS) <= set(
        sentinels_by_selector
    ):
        raise VerificationError(
            "independent outcome-route blocker sentinel lacks a disposition sentinel"
        )

    member_routes: dict[str, set[str]] = defaultdict(set)
    for member_contract_id, _, route_id in sentinels_by_selector:
        member_routes[member_contract_id].add(route_id)

    matched_selectors: set[tuple[str, str, str]] = set()
    for exact in exact_rows:
        member_contract_id = exact["member_contract_id"]
        suffix = outcome_suffix(exact)
        for route_id in member_routes.get(member_contract_id, set()):
            exact_selector = (member_contract_id, suffix, route_id)
            wildcard_selector = (
                member_contract_id,
                OUTCOME_ROUTE_SENTINEL_ALL_OUTCOMES,
                route_id,
            )
            selector = (
                exact_selector
                if exact_selector in sentinels_by_selector
                else wildcard_selector
            )
            expected = sentinels_by_selector.get(selector)
            if expected is None:
                continue
            matched_selectors.add(selector)
            authority = outcome_authority_by_key[(exact["contract_id"], route_id)]
            actual = (authority["disposition"], authority["predicate_id"])
            if actual != expected:
                raise VerificationError(
                    "independent outcome-route sentinel differs from static authority "
                    f"for {exact['contract_id']}/{route_id}: "
                    f"expected={expected} actual={actual}"
                )
            expected_blocker = INDEPENDENT_OUTCOME_ROUTE_BLOCKER_SENTINELS.get(
                selector
            )
            if (
                expected_blocker is not None
                and authority["blocker_ids"] != expected_blocker
            ):
                raise VerificationError(
                    "independent outcome-route blocker sentinel differs from "
                    f"static authority for {exact['contract_id']}/{route_id}: "
                    f"expected={expected_blocker} actual={authority['blocker_ids']}"
                )

    dead_selectors = set(sentinels_by_selector) - matched_selectors
    if dead_selectors:
        raise VerificationError(
            "independent outcome-route sentinel matches no exact authority cell: "
            f"{sorted(dead_selectors)[0]}"
        )


def read_outcome_authority(
    exact_rows: list[dict[str, str]], routes: dict[str, dict[str, str]]
) -> tuple[
    list[dict[str, str]],
    dict[tuple[str, str], dict[str, str]],
]:
    verify_frozen_static_authority(OUTCOME_AUTHORITY_PATH)
    raw = OUTCOME_AUTHORITY_PATH.read_bytes()
    if b"\r" in raw or not raw.endswith(b"\n"):
        raise VerificationError("outcome authority must use strict LF termination")
    with OUTCOME_AUTHORITY_PATH.open(newline="") as handle:
        header = tuple(csv.DictReader(handle, delimiter="\t").fieldnames or ())
    if header != OUTCOME_AUTHORITY_FIELDS:
        raise VerificationError("outcome authority header differs from schema")
    rows = read_tsv(OUTCOME_AUTHORITY_PATH)
    exact_by_contract = {row["contract_id"]: row for row in exact_rows}
    if len(exact_by_contract) != 303:
        raise VerificationError("outcome authority source must contain 303 exact contracts")
    expected_keys = expected_outcome_authority_keys(exact_rows)
    by_key: dict[tuple[str, str], dict[str, str]] = {}
    seen_ids: set[str] = set()
    choice_sets: dict[
        tuple[str, str], dict[str, set[str]]
    ] = defaultdict(lambda: defaultdict(set))
    previous_id = ""

    for row in rows:
        if row["schema_version"] != OUTCOME_AUTHORITY_SCHEMA_VERSION:
            raise VerificationError("outcome authority schema changed")
        authority_id = row["authority_entry_id"]
        if authority_id in seen_ids:
            raise VerificationError(f"duplicate outcome authority ID {authority_id}")
        if previous_id and authority_id <= previous_id:
            raise VerificationError("outcome authority rows must sort by authority_entry_id")
        previous_id = authority_id
        seen_ids.add(authority_id)

        contract_id = row["contract_id"]
        exact = exact_by_contract.get(contract_id)
        if exact is None:
            raise VerificationError(f"outcome authority names unknown contract {contract_id}")
        if row["member_contract_id"] != exact["member_contract_id"]:
            raise VerificationError(f"outcome authority member mismatch for {contract_id}")
        if row["outcome_id"] != exact["outcome_id"]:
            raise VerificationError(f"outcome authority outcome mismatch for {contract_id}")

        route_id = row["route_id"]
        if route_id not in OUTCOME_AUTHORITY_ROUTE_IDS or route_id not in routes:
            raise VerificationError(
                f"outcome authority names non-frozen or unknown route {route_id}"
            )
        disposition = row["disposition"]
        if disposition not in OUTCOME_AUTHORITY_DISPOSITIONS:
            raise VerificationError(
                f"outcome authority has unknown disposition {disposition}"
            )
        if row["authority_status"] != OUTCOME_AUTHORITY_STATUS:
            raise VerificationError(f"outcome authority status changed for {authority_id}")
        if row["authority_basis"] in {"", "NONE"}:
            raise VerificationError(f"outcome authority lacks a basis for {authority_id}")

        if disposition in {"REQUIRED", "FORBIDDEN"}:
            expected_aux = {
                "choice_set_id": "NONE",
                "choice_alternative_id": "NONE",
                "predicate_id": "NONE",
                "blocker_ids": "NONE",
            }
            for field, expected in expected_aux.items():
                if row[field] != expected:
                    raise VerificationError(
                        f"{authority_id} gives {disposition} an unexpected {field}"
                    )
        elif disposition == "CONDITIONAL_WITH_FROZEN_PREDICATE":
            if (
                row["predicate_id"] in {"", "NONE"}
                or row["choice_set_id"] != "NONE"
                or row["choice_alternative_id"] != "NONE"
                or row["blocker_ids"] != "NONE"
            ):
                raise VerificationError(
                    f"{authority_id} has a malformed frozen conditional"
                )
        elif disposition == "REQUIRE_ONE_OF":
            if (
                row["choice_set_id"] in {"", "NONE"}
                or row["choice_alternative_id"] in {"", "NONE"}
                or row["predicate_id"] != "NONE"
                or row["blocker_ids"] != "NONE"
            ):
                raise VerificationError(f"{authority_id} has a malformed choice row")
            choice_sets[(contract_id, row["choice_set_id"])][
                row["choice_alternative_id"]
            ].add(route_id)
        elif (
            row["blocker_ids"] in {"", "NONE"}
            or row["choice_set_id"] != "NONE"
            or row["choice_alternative_id"] != "NONE"
            or row["predicate_id"] != "NONE"
        ):
            raise VerificationError(f"{authority_id} has malformed unresolved blockers")

        expected_id = f"OUTCOME-ROUTE::{contract_id}::{route_id}"
        if disposition == "REQUIRE_ONE_OF":
            expected_id += (
                f"::{row['choice_set_id']}::{row['choice_alternative_id']}"
            )
        if authority_id != expected_id:
            raise VerificationError(f"outcome authority ID mismatch for {authority_id}")
        expected_hash = row_sha256(
            row,
            [
                field
                for field in OUTCOME_AUTHORITY_FIELDS
                if field != "authority_entry_sha256"
            ],
        )
        if row["authority_entry_sha256"] != expected_hash:
            raise VerificationError(f"outcome authority hash mismatch for {authority_id}")

        key = (contract_id, route_id)
        if key in by_key:
            raise VerificationError(f"duplicate outcome authority route key {key}")
        by_key[key] = row

    missing = expected_keys - set(by_key)
    extra = set(by_key) - expected_keys
    if missing or extra:
        sample = sorted(missing or extra)[:5]
        raise VerificationError(
            "outcome authority must be the exact 303-by-14 complement; "
            f"missing={len(missing)} extra={len(extra)} first={sample}"
        )
    if len(rows) != 4242:
        raise VerificationError("outcome authority must contain exactly 4,242 rows")

    for (contract_id, choice_set_id), alternatives in choice_sets.items():
        if len(alternatives) < 2:
            raise VerificationError(
                f"choice set {contract_id}/{choice_set_id} must contain at least two alternatives"
            )
        bundles = [frozenset(route_ids) for route_ids in alternatives.values()]
        if len(set(bundles)) != len(bundles):
            raise VerificationError(
                f"choice set {contract_id}/{choice_set_id} repeats an alternative bundle"
            )
        flattened = [route_id for bundle in bundles for route_id in bundle]
        if len(flattened) != len(set(flattened)):
            raise VerificationError(
                f"choice set {contract_id}/{choice_set_id} shares a route across alternatives"
            )

    typed_place_rows = [
        row for row in rows if row["route_id"] == "CORE-TYPED-PLACE"
    ]
    if any(row["disposition"] != "FORBIDDEN" for row in typed_place_rows):
        raise VerificationError("CORE-TYPED-PLACE gains unsupported outcome authority")

    validate_independent_outcome_route_sentinels(exact_rows, by_key)

    return rows, by_key


def audit_outcome_authority_repertoire(
    exact_rows: list[dict[str, str]], rows: list[dict[str, str]]
) -> dict[str, int]:
    repertoire_keys = outcome_repertoire_authority_keys(exact_rows)
    missing_positive = [
        (row["contract_id"], row["route_id"], row["disposition"])
        for row in rows
        if row["disposition"] != "FORBIDDEN"
        and (row["contract_id"], row["route_id"]) not in repertoire_keys
    ]
    return {
        "repertoire_missing_authorized_routes": len(missing_positive),
        "repertoire_routes_explicitly_forbidden": sum(
            row["disposition"] == "FORBIDDEN"
            and (row["contract_id"], row["route_id"]) in repertoire_keys
            for row in rows
        ),
    }


def outcome_predicate_authority_tuple(
    row: dict[str, str],
) -> tuple[str, str, str, str, str, str]:
    return (
        row["guard_kind"],
        row["guard_source_field_ids"],
        row["guard_expression"],
        row["true_route_disposition"],
        row["false_route_disposition"],
        row["source_anchor"],
    )


def outcome_predicate_matches_frozen(row: dict[str, str]) -> bool:
    return FROZEN_OUTCOME_PREDICATES.get(
        row["predicate_id"]
    ) == outcome_predicate_authority_tuple(row)


def read_predicate_authority(
    outcome_authority_rows: list[dict[str, str]],
) -> dict[str, dict[str, str]]:
    verify_frozen_static_authority(PREDICATE_AUTHORITY_PATH)
    raw = PREDICATE_AUTHORITY_PATH.read_bytes()
    if b"\r" in raw or not raw.endswith(b"\n"):
        raise VerificationError("outcome predicate authority must use strict LF termination")
    with PREDICATE_AUTHORITY_PATH.open(newline="") as handle:
        header = tuple(csv.DictReader(handle, delimiter="\t").fieldnames or ())
    if header != PREDICATE_AUTHORITY_FIELDS:
        raise VerificationError("outcome predicate authority header differs from schema")
    rows = read_tsv(PREDICATE_AUTHORITY_PATH)
    by_id: dict[str, dict[str, str]] = {}
    previous_id = ""
    for row in rows:
        predicate_id = row["predicate_id"]
        if predicate_id in by_id:
            raise VerificationError(f"duplicate outcome predicate {predicate_id}")
        if previous_id and predicate_id <= previous_id:
            raise VerificationError("outcome predicates must sort by predicate_id")
        previous_id = predicate_id
        if row["schema_version"] != OUTCOME_PREDICATE_SCHEMA_VERSION:
            raise VerificationError("outcome predicate schema changed")
        if row["guard_kind"] not in OUTCOME_PREDICATE_GUARD_KINDS:
            raise VerificationError(f"outcome predicate {predicate_id} has unknown guard kind")
        if row["guard_source_field_ids"] in {"", "NONE"}:
            raise VerificationError(f"outcome predicate {predicate_id} has no source fields")
        if row["guard_expression"] in {"", "NONE"} or row["source_anchor"] in {
            "",
            "NONE",
        }:
            raise VerificationError(f"outcome predicate {predicate_id} is unanchored")
        if {
            row["true_route_disposition"],
            row["false_route_disposition"],
        } != {"REQUIRED", "FORBIDDEN"}:
            raise VerificationError(
                f"outcome predicate {predicate_id} must select exactly REQUIRED versus FORBIDDEN"
            )
        if row["authority_status"] != OUTCOME_AUTHORITY_STATUS:
            raise VerificationError(f"outcome predicate {predicate_id} status changed")
        source_fields = split_ids(row["guard_source_field_ids"])
        if row["guard_kind"] in {
            "EXACT_SOURCE_FIELD_EQUAL",
            "EXACT_SOURCE_FIELD_IN_SET",
        } and len(source_fields) != 1:
            raise VerificationError(
                f"outcome predicate {predicate_id} exact-source guard must name one field"
            )
        if row["guard_kind"] == "EXACT_SOURCE_FIELD_EQUAL":
            prefix = f"{source_fields[0]}=="
            if not row["guard_expression"].startswith(prefix) or row[
                "guard_expression"
            ][len(prefix) :] in {"", "NONE"}:
                raise VerificationError(
                    f"outcome predicate {predicate_id} has a malformed equality guard"
                )
        if row["guard_kind"] == "EXACT_SOURCE_FIELD_IN_SET":
            prefix = f"{source_fields[0]} in {{"
            expression = row["guard_expression"]
            if not expression.startswith(prefix) or not expression.endswith("}"):
                raise VerificationError(
                    f"outcome predicate {predicate_id} has a malformed set guard"
                )
            values = tuple(expression[len(prefix) : -1].split(","))
            if (
                not values
                or any(value in {"", "NONE"} for value in values)
                or tuple(sorted(set(values))) != values
            ):
                raise VerificationError(
                    f"outcome predicate {predicate_id} set literals are noncanonical"
                )
        if not outcome_predicate_matches_frozen(row):
            raise VerificationError(
                f"outcome predicate {predicate_id} is absent from or differs from the frozen verifier vocabulary"
            )
        expected_hash = row_sha256(
            row,
            [
                field
                for field in PREDICATE_AUTHORITY_FIELDS
                if field != "predicate_sha256"
            ],
        )
        if row["predicate_sha256"] != expected_hash:
            raise VerificationError(f"outcome predicate hash mismatch for {predicate_id}")
        by_id[predicate_id] = row

    referenced = {
        row["predicate_id"]
        for row in outcome_authority_rows
        if row["disposition"] == "CONDITIONAL_WITH_FROZEN_PREDICATE"
    }
    if set(by_id) != referenced or set(by_id) != set(FROZEN_OUTCOME_PREDICATES):
        raise VerificationError(
            "outcome predicate registry must exactly equal conditional authority references and the frozen vocabulary"
        )
    return by_id


def evaluate_frozen_predicate(
    predicate: dict[str, str],
    exact: dict[str, str],
    context: dict[str, str],
) -> str:
    if predicate["guard_kind"] == "RUNTIME_BRANCH_GUARD":
        return "RUNTIME_UNRESOLVED"
    if predicate["guard_kind"] == "TRACE_CLASSIFIER_ONLY":
        return "TRACE_CLASSIFIER_UNOBSERVED"
    field_id = split_ids(predicate["guard_source_field_ids"])[0]
    if field_id in context:
        actual = context[field_id]
    elif field_id in exact:
        actual = exact[field_id]
    else:
        raise VerificationError(
            f"frozen predicate {predicate['predicate_id']} names unavailable field {field_id}"
        )
    expression = predicate["guard_expression"]
    if predicate["guard_kind"] == "EXACT_SOURCE_FIELD_EQUAL":
        expected = expression[len(field_id) + 2 :]
        result = actual == expected
    elif predicate["guard_kind"] == "EXACT_SOURCE_FIELD_IN_SET":
        prefix = f"{field_id} in {{"
        expected_values = set(expression[len(prefix) : -1].split(","))
        result = actual in expected_values
    else:
        raise VerificationError(
            f"frozen predicate {predicate['predicate_id']} has no evaluator"
        )
    return "TRUE" if result else "FALSE"


