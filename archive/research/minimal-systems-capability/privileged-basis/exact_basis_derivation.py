        is_choice = bool(step) and step["route_applicability"].startswith("CHOICE_")
        kind = obligation["obligation_kind"]
        if kind == "REQUIRE_ACTIVATED":
            required += 1
            if activated:
                satisfied += 1
            else:
                missing_by_context[context_id] += 1
        elif kind == "FORBID_ACTIVATED":
            forbidden += 1
            if activated:
                forbidden_violations += 1
