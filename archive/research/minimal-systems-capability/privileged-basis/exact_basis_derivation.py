def member_source_preservation(member: str, group: str) -> str:
    if group == "COPY":
        return "SOURCE_REMAINS_FULL_DESTINATION_ONLY_CHANGES"
    if group == "RESHAPE":
        return "SAME_CARRIER_SAME_BYTES_OLD_PARTITION_RETIRED"
    if group == "ADOPT":
        return "SAME_FULL_STORAGE_ROOT_AND_PAYLOAD_IDENTITY"
    if group in {"BORROW", "ITER", "READ_BEHAVIOR", "META"}:
        return "SOURCE_OWNER_RETAINED_OR_EXACTLY_TRANSFERRED_AS_DECLARED"
    if group == "NEGATIVE":
        return "NO_ADMITTED_RUNTIME_TRANSITION"
    return "EXACT_SOURCE_OWNER_ROLE_AND_STATE_EQUATIONS"


