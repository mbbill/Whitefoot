class VerificationError(RuntimeError):
    pass


def sha256_path(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


def verify_frozen_static_authority(path: Path) -> None:
    expected = FROZEN_STATIC_AUTHORITY_SHA256S[path]
    actual = sha256_path(path)
    if actual != expected:
        raise VerificationError(
            f"frozen static authority bytes changed for {path.name}: "
            f"expected={expected} actual={actual}"
        )


def read_tsv(path: Path) -> list[dict[str, str]]:
    with path.open(newline="") as handle:
        return list(csv.DictReader(handle, delimiter="\t"))


def split_ids(value: str) -> tuple[str, ...]:
    if value in ("", "NONE"):
        return ()
    return tuple(part for part in value.split(",") if part and part != "NONE")


def join_ids(values: list[str] | tuple[str, ...] | set[str]) -> str:
    materialized = sorted(set(values))
    return ",".join(materialized) if materialized else "NONE"


def member_group_map() -> dict[str, str]:
    result: dict[str, str] = {}
    for group, members in MEMBER_GROUPS.items():
        for member in members:
            if member in result:
                raise VerificationError(f"member {member} occurs in both {result[member]} and {group}")
            result[member] = group
    return result


def algorithm_routes(member: str, group: str) -> tuple[str, ...]:
    if group == "NEGATIVE":
        if member in STATIC_REJECTION_MEMBERS:
            return ("NEG-STATIC-REJECTION",)
        if member in LATER_DOMAIN_MEMBERS:
            return ("NEG-LATER-DOMAIN",)
        return ("NEG-POLICY-CONDITIONAL",)
    if group == "ITER":
        route = {
            "DENSE-ITER-OWN": "PROTOCOL-CURSOR-OWN",
            "DENSE-ITER-SHARED": "PROTOCOL-CURSOR-SHARED",
            "DENSE-ITER-UNIQ": "PROTOCOL-CURSOR-UNIQ",
        }[member]
        routes = ["CORE-PARTITION-BORROW", route, "PROTOCOL-OPAQUE-OWNER"]
        if member == "DENSE-ITER-OWN":
            routes.extend(("CORE-TAKE-PUT", "PROTOCOL-PLAN-DISPOSE"))
        return tuple(routes)
    routes = list(GROUP_ROUTES[group])
    if member == "DENSE-EAGER-SPLICE":
        routes = [
            "CALLABLE-PRODUCER" if route == "CALLABLE-USER" else route
            for route in routes
        ]
    if member in {"DENSE-INDEX-SHARED", "DENSE-INDEX-UNIQ"}:
        routes.append("CALLABLE-USER")
    if member == "DENSE-INIT-COPY":
        routes.append("PROTOCOL-EXACT-FOCUS")
    if member == "DENSE-INTO-BOXED":
        routes.extend(
            ("CORE-RESHAPE-PARTITION", "CORE-REPACKAGE-FULL-STORAGE")
        )
    if member == "DENSE-TAKE-WITH-DEFAULT":
        routes.append("CALLABLE-DEFAULT")
    return tuple(routes)


def behavior_premise(member: str, group: str) -> tuple[str, str]:
    if group == "COPY":
        return "CORE_COPY", "CORE-COPY"
    if group in ("CLONE_BUILD", "CLONE_MUTATE"):
        return "CLONE", "CALLABLE-CLONE"
    if member in {"DENSE-COLLECT", "DENSE-EXTEND-ITER"}:
        return "ITERATOR", "CALLABLE-PRODUCER"
    if group == "PRODUCER" or member == "DENSE-EAGER-SPLICE":
        return "PRODUCER", "CALLABLE-PRODUCER"
    if group == "CONVERT":
        return "DECLARED_CONVERSION", "CALLABLE-PRODUCER"
    if member == "DENSE-TAKE-WITH-DEFAULT":
        return "DEFAULT", "CALLABLE-DEFAULT"
    if member in {"DENSE-INDEX-SHARED", "DENSE-INDEX-UNIQ"}:
        return "STATIC_INDEX", "CALLABLE-USER"
    if group in ("READ_BEHAVIOR", "CALLABLE_COMPACT", "SORT_STABLE", "SORT_UNSTABLE"):
        return "USER_BEHAVIOR", "CALLABLE-USER"
    if group == "DISPOSE":
        return "DISPOSITION_PLAN", "PROTOCOL-PLAN-DISPOSE"
    return "NONE", "NONE"


def negative_disposition(member: str) -> tuple[str, str]:
    if member in STATIC_REJECTION_MEMBERS:
        return "STATIC_REJECTION", "RAW-SPELLING-REJECTION"
    if member in LATER_DOMAIN_MEMBERS:
        return "LATER_DOMAIN_DEPENDENCY", "F-RECURSIVE"
    if member in OD4_EXCLUSION_MEMBERS:
        return "OWNER_POLICY_UNRESOLVED", "OD-4"
    raise VerificationError(f"unknown negative member {member}")


def basis_disposition(row: dict[str, str]) -> tuple[str, str]:
    status = row["status"]
    if status == "REQUIRED_IN_LOCK":
        return "PAPER_ROUTE_COVERED_TRACE_NOT_MECHANIZED", "MECHANIZED-TRACE-NOT-PRODUCED"
    if status == "EXCLUDED_BLOCKS_NAMED_CLAIM":
        return negative_disposition(row["member_contract_id"])
    if status == "CONDITIONAL_ON_UNRESOLVED_THREE_WAY_OD-4":
        return "OWNER_POLICY_UNRESOLVED", "OD-4"
    if status == "BLOCKED_UNTIL_UNKNOWN_LENGTH_RECOVERY_RULE_IS_OWNER_APPROVED":
        return "SOURCE_BLOCKED", "UNKNOWN-LENGTH-RECOVERY"
    if status == "BLOCKED_UNKNOWN_LENGTH_RECOVERY_AND_CONDITIONAL_ON_UNRESOLVED_THREE_WAY_OD-4":
        return "SOURCE_BLOCKED_AND_OWNER_POLICY_UNRESOLVED", "OD-4,UNKNOWN-LENGTH-RECOVERY"
    raise VerificationError(f"unknown source status {status}")


def exit_route(row: dict[str, str]) -> str:
    if row["status"] == "EXCLUDED_BLOCKS_NAMED_CLAIM":
        member = row["member_contract_id"]
        if member in STATIC_REJECTION_MEMBERS:
            return "NEG-STATIC-REJECTION"
        if member in LATER_DOMAIN_MEMBERS:
            return "NEG-LATER-DOMAIN"
        return "NEG-POLICY-CONDITIONAL"
    if row["post_state"] == "NO_RECOVERABLE_POSTSTATE" or row["pre_abort_invariant"] not in (
        "NOT_APPLICABLE_NORMAL_RESULT",
        "NOT_APPLICABLE_RECOVERABLE_RESULT",
    ):
        return "EXIT-ABORT-SAFE"
    if row["pre_abort_invariant"] == "NOT_APPLICABLE_RECOVERABLE_RESULT":
        return "EXIT-RECOVERABLE-PRECOMMIT"
    return "EXIT-NORMAL-RESEAL"


def legacy_alias_mismatches(row: dict[str, str]) -> tuple[str, ...]:
    member = row["member_contract_id"]
    capabilities = set(split_ids(row["capability_ids"]))
    result: list[str] = []
    if member in {"DENSE-COPY-FROM", "DENSE-COPY-WITHIN"} and "OW-RELOCATE" in capabilities:
        result.append("OW-RELOCATE-AS-CORE-COPY")
    if member == "DENSE-INIT-COPY" and "OW-CLONE" in capabilities:
        result.append("OW-CLONE-AS-CORE-COPY")
    if member in {"DENSE-FILL-WITH", "DENSE-RESIZE-WITH"} and "OW-CLONE" in capabilities:
        result.append("OW-CLONE-AS-PRODUCER")
    if member == "DENSE-INTO-FLATTENED" and capabilities & {"OW-MOVEOUT", "OW-RELOCATE", "OW-DROP"}:
        result.append("OWNER-TRAFFIC-AS-RESHAPE")
    if member == "DENSE-INTO-OWNER":
        if "OW-CLONE" in capabilities:
            result.append("OW-CLONE-AS-OWNER-REPACK")
        if capabilities & {"OW-MOVEOUT", "OW-RELOCATE", "OW-DROP"}:
            result.append("OWNER-TRAFFIC-AS-OWNER-REPACK")
        if "FL-ALLOC" in capabilities:
            result.append("ALLOC-CAPABILITY-ON-ZERO-ALLOC-REPACK")
    if member in {"DENSE-DEFAULT", "DENSE-NEW", "DENSE-META"} and "OW-INIT" in capabilities:
        result.append("OW-INIT-AS-METADATA-FORMATION")
    return tuple(result)


def applicable_authority_bindings(
    exact: dict[str, str], authority_by_member: dict[str, list[dict[str, str]]]
) -> tuple[list[str], list[str]]:
    branches = set(split_ids(exact["payload_branch_ids"]))
    required: list[str] = []
    overlays: list[str] = []
    for binding in authority_by_member[exact["member_contract_id"]]:
        kind = binding["binding_kind"]
        if kind == "MEMBER" and binding["applicability"] == "REQUIRED":
            required.append(f"{binding['capability_id']}@MEMBER[REQUIRED]")
        elif kind == "DEFERRED_OVERLAY" and binding["overlay_branch_id"] in branches:
            overlays.append(
                f"{binding['capability_id']}@{binding['overlay_branch_id']}[{binding['applicability']}]"
            )
        elif kind == "EXCLUDED_MEMBER" and exact["status"] == "EXCLUDED_BLOCKS_NAMED_CLAIM":
            required.append(f"{binding['capability_id']}@EXCLUDED_MEMBER[{binding['applicability']}]")
    return sorted(set(required)), sorted(set(overlays))


def overlay_routes(required: list[str], overlays: list[str], exact: dict[str, str]) -> tuple[str, ...]:
    bindings = required + overlays
    result: list[str] = []
    if any(binding.startswith("BR-STORED@") for binding in bindings) or exact["stored_borrow_route_ids"] != "NONE":
        result.append("PROTOCOL-STORED-BORROW")
    if any(binding.startswith(("BR-PROV@", "BR-RESULT@", "BR-STORED@")) for binding in bindings):
        result.append("FACT-BORROW")
    if any(binding.startswith(("FT-STATE@", "FT-REFINE@", "FT-IDENTITY@")) for binding in bindings) or set(
        split_ids(exact["capability_ids"])
    ) & {"FT-STATE", "FT-REFINE", "FT-IDENTITY"}:
        result.append("FACT-STATE")
    return tuple(result)


def structural_status(source_status: str) -> str:
    if source_status == "REQUIRED_IN_LOCK":
        return "PAPER_ROUTE_COVERED_COST_ALGEBRA_NOT_MECHANIZED"
    if source_status == "EXCLUDED_BLOCKS_NAMED_CLAIM":
        return "NO_POSITIVE_COST_CREDIT"
    if source_status.startswith("BLOCKED"):
        return "NO_POSITIVE_COST_CREDIT_BLOCKED"
    return "PAPER_ROUTE_POLICY_UNRESOLVED"


def row_sha256(row: dict[str, str], fields: tuple[str, ...] | list[str] | None = None) -> str:
    selected = fields if fields is not None else tuple(row)
    material = "\t".join(row[field] for field in selected)
    return hashlib.sha256(material.encode()).hexdigest()


def render_tsv(rows: list[dict[str, str]], fields: tuple[str, ...]) -> bytes:
    buffer = io.StringIO(newline="")
    writer = csv.DictWriter(buffer, fieldnames=fields, delimiter="\t", lineterminator="\n")
    writer.writeheader()
    writer.writerows(rows)
    return buffer.getvalue().encode()


def outcome_suffix(row: dict[str, str]) -> str:
    return row["outcome_id"].rsplit(".OUT.", 1)[-1]


def outcome_path_kind(row: dict[str, str]) -> str:
    if row["status"] == "EXCLUDED_BLOCKS_NAMED_CLAIM":
        return "NEGATIVE_OR_DEPENDENCY"
    route = exit_route(row)
    suffix = outcome_suffix(row)
    if route == "EXIT-NORMAL-RESEAL":
        return "NORMAL"
    if route == "EXIT-RECOVERABLE-PRECOMMIT":
        if suffix == "ALLOCATION_ERROR_RETURN":
            return "RECOVERABLE_ALLOCATION_FAILURE"
        return "RECOVERABLE_CHECK_OR_POLICY_FAILURE"
    if row["behavior_calls"] != "ZERO":
        return "BEHAVIOR_ABORT_PREFIX"
    if suffix == "OOM_ABORT":
        return "ALLOCATION_ABORT_PREFIX"
    return "CHECK_OR_POLICY_ABORT_PREFIX"


def allocation_capable_members(exact_rows: list[dict[str, str]]) -> set[str]:
    result: set[str] = set()
    for row in exact_rows:
        suffix = outcome_suffix(row)
        if suffix in {"OOM_ABORT", "ALLOCATION_ERROR_RETURN"}:
            result.add(row["member_contract_id"])
        if "A1" in row["allocation_after"] and "A1" not in row["allocation_before"]:
            result.add(row["member_contract_id"])
    return result


def outer_allocator_route_applicability(
    row: dict[str, str], allocator_capable: set[str]
) -> str:
    member = row["member_contract_id"]
    suffix = outcome_suffix(row)
    if row["status"] == "EXCLUDED_BLOCKS_NAMED_CLAIM":
        return "FORBIDDEN_NEGATIVE_ROUTE"
    if suffix in {"OOM_ABORT", "ALLOCATION_ERROR_RETURN", "SUCCESS_GROW", "SUCCESS_RELOCATE"}:
        return "REQUIRED_BY_EXACT_OUTCOME"
    if suffix in {
        "SUCCESS_NO_GROW",
        "SUCCESS_NO_CHANGE",
        "CAPACITY_OVERFLOW_TRAP",
        "CAPACITY_ERROR_RETURN",
        "BOUNDS_TRAP",
        "PRECONDITION_TRAP",
        "CHECKED_ERROR",
        "OVERFILL_REJECTED",
        "UNDERFILL_CLOSE_REJECTED",
    }:
        return "FORBIDDEN_BY_EXACT_OUTCOME"
    if member not in allocator_capable:
        if "OD-4" in row["status"]:
            return "UNRESOLVED_OD4_OUTER_ALLOCATION_POLICY"
        return "FORBIDDEN_NO_STRUCTURED_OUTER_ALLOCATION_BRANCH"
    if row["behavior_calls"] != "ZERO" and exit_route(row) == "EXIT-ABORT-SAFE":
        return "UNRESOLVED_OUTER_ALLOCATION_BEFORE_BEHAVIOR_ABORT"
    return "UNRESOLVED_OUTER_ALLOCATION_ON_NORMAL_BRANCH"


def behavior_route_applicability(row: dict[str, str], behavior_route: str) -> str:
    if behavior_route == "NONE":
        return "NOT_APPLICABLE"
    if row["behavior_calls"] == "ZERO":
        return "FORBIDDEN_ZERO_CALL_OUTCOME"
    if exit_route(row) == "EXIT-ABORT-SAFE":
        return "REQUIRED_EXACT_ABORT_PREFIX"
    return "REQUIRED_BY_EXACT_OUTCOME"


_OD1_AUTHORITY_CACHE: dict[tuple[str, str], dict[str, str]] | None = None


def od1_authority_by_key() -> dict[tuple[str, str], dict[str, str]]:
    global _OD1_AUTHORITY_CACHE
    if _OD1_AUTHORITY_CACHE is None:
        rows = read_tsv(OD1_PATH)
        cache = {(row["policy_variant_id"], row["member_contract_id"]): row for row in rows}
        if len(cache) != len(rows):
            raise VerificationError("OD1 policy registry contains duplicate policy/member keys")
        _OD1_AUTHORITY_CACHE = cache
    return _OD1_AUTHORITY_CACHE


def route_applicability(
    row: dict[str, str],
    route_id: str,
    allocator_capable: set[str],
    behavior_route: str,
) -> str:
    path = outcome_path_kind(row)
    allocator_mode = outer_allocator_route_applicability(row, allocator_capable)
    behavior_mode = behavior_route_applicability(row, behavior_route)
    if route_id.startswith("NEG-"):
        return "REQUIRED_NEGATIVE_DISPOSITION" if path == "NEGATIVE_OR_DEPENDENCY" else "FORBIDDEN_POSITIVE_ROUTE"
    if route_id == "FAIL-ALLOC-EXACT":
        return allocator_mode
    if route_id == "FAIL-RESERVE-FIRST":
        authority = od1_authority_by_key().get(
            (row["policy_variant_id"], row["member_contract_id"])
        )
        if authority is None:
            return "FORBIDDEN_NO_OD1_MEMBER_AUTHORITY"
        if authority["member_class"] == "CONSTRUCTOR_NOT_MUTATOR_FIXED_DIVERGENT":
            return "NOT_APPLICABLE_FIXED_DIVERGENT_CONSTRUCTOR"
        if authority["member_class"] != "GROWTH_CAPABLE_MUTATOR":
            return "UNRESOLVED_UNKNOWN_OD1_MEMBER_CLASS"
        if path.startswith("RECOVERABLE_"):
            return "REQUIRED_PRECOMMIT_RECOVERY_DISCIPLINE"
        if outcome_suffix(row) in {"SUCCESS_NO_GROW", "SUCCESS_NO_CHANGE"}:
            return "REQUIRED_PROOF_DISCIPLINE_NO_RUNTIME_RESERVE"
        return "REQUIRED_CAPACITY_BEFORE_DESTRUCTIVE_COMMIT_DISCIPLINE"
    if route_id.startswith("CALLABLE-"):
        if route_id != behavior_route:
            return "FORBIDDEN_BY_PREMISE_CLASS"
        return behavior_mode
    if route_id == "FAIL-CHECKED-ARITH":
        if path == "NEGATIVE_OR_DEPENDENCY":
            return "FORBIDDEN_NEGATIVE_ROUTE"
        return "REQUIRED_CHECKED_PRECONDITION_OR_ARITHMETIC"
    if route_id in {"PROTOCOL-OPAQUE-OWNER", "PROTOCOL-EXACT-FOCUS", "PROTOCOL-PLAN-DISPOSE"}:
        if path == "NEGATIVE_OR_DEPENDENCY":
            return "FORBIDDEN_NEGATIVE_ROUTE"
        if path in {"CHECK_OR_POLICY_ABORT_PREFIX", "RECOVERABLE_CHECK_OR_POLICY_FAILURE"}:
            return "REQUIRED_PRESTATE_PRESERVATION_ONLY"
        return "REQUIRED_RESOURCE_DISCIPLINE"
    if path in {"CHECK_OR_POLICY_ABORT_PREFIX", "RECOVERABLE_CHECK_OR_POLICY_FAILURE"}:
        return "FORBIDDEN_BEFORE_EXACT_COMMIT"
    if path in {"ALLOCATION_ABORT_PREFIX", "RECOVERABLE_ALLOCATION_FAILURE"}:
        return "FORBIDDEN_BEFORE_ALLOCATION_ACQUISITION"
    if path == "BEHAVIOR_ABORT_PREFIX":
        return "CONDITIONAL_EXACT_PREFIX_BEFORE_ABORT"
    if path == "NEGATIVE_OR_DEPENDENCY":
        return "FORBIDDEN_NEGATIVE_ROUTE"
    return "REQUIRED_OR_DATA_CONDITIONAL_NORMAL_ROUTE"


def outcome_routes(
    row: dict[str, str], group: str, allocator_capable: set[str]
) -> tuple[str, ...]:
    member = row["member_contract_id"]
    _, behavior_route = behavior_premise(member, group)
    selected: list[str] = []
    for route_id in algorithm_routes(member, group):
        applicability = route_applicability(row, route_id, allocator_capable, behavior_route)
        if not applicability.startswith("FORBIDDEN") and applicability != "BEHAVIOR_OWNED_ONLY_OUTER_FORBIDDEN":
            selected.append(route_id)
    selected.append(exit_route(row))
    return tuple(sorted(set(selected)))


def route_applicability_is_activated(applicability: str) -> bool:
    return applicability.startswith("REQUIRED")


def zero_payload_traffic_required(row: dict[str, str]) -> str:
    member = row["member_contract_id"]
    if member in {"DENSE-INTO-FLATTENED", "DENSE-INTO-OWNER"} and outcome_path_kind(row) == "NORMAL":
        return "YES_EXACT_ROUTE"
    if member in MEMBER_GROUPS["EMPTY"] | MEMBER_GROUPS["META"] | STATIC_REJECTION_MEMBERS:
        return "YES_NO_PAYLOAD_ROUTE"
    return "NO_EXACT_ZERO_CLAIM"


def zero_outer_allocation_required(
    row: dict[str, str], allocator_capable: set[str]
) -> str:
    mode = outer_allocator_route_applicability(row, allocator_capable)
    if row["member_contract_id"] == "DENSE-INTO-OWNER" and outcome_path_kind(row) == "NORMAL":
        return "YES_EXACT_ZERO_ALLOCATION"
    if mode.startswith("FORBIDDEN") or mode == "BEHAVIOR_OWNED_ONLY_OUTER_FORBIDDEN":
        return "YES_OUTER_ALLOCATOR_FORBIDDEN"
    return "NO_EXACT_ZERO_CLAIM"


